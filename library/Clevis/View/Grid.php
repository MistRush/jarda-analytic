    public function getCurrentValue() {
        return $this->getId() . '.getIdentifier()';
    }


    /**
     * On init script type notification
     *
     * @param Clevis_View_Script $script
     */
    protected function onInitScript(Clevis_View_Script & $script) {
        parent::onInitScript($script);

        $id = $this->getId();

        // On load add formatters
        $addOnLoad = 'require(["clevis/ready!"], function() {';
        $addOnLoad .= $id . '.columnFormatters = {};';
        foreach ( $this->columnFormatters as $columnField => $columnFormat ) {
            $formatter = $columnFormat->toArray($this);
            $formatterString = '';
            foreach ( $formatter as $name => $value ) {
                if ( strlen($formatterString) > 0 ) {
                    $formatterString .= ', ';
                }
                if ( $value === null) {
                    $value = 'null';
                } else if ( is_array($value) ) {
                    $value = Zend_Json::encode($value);
                } else if ( !startsWith($value, 'function') ) {
                    $value = str_replace('"','\\\'',$value);
                    $value = '\'' . $value . '\'';
                }
                $formatterString .= $name . ': ' . $value;
            }

            //$value = str_replace('"', '\\"', $value);
            $addOnLoad .= $id . '.columnFormatters["' . $columnField . '"] = ';
            //$addOnLoad .= Zend_Json::encode($formatter);
            $addOnLoad .= '{' . $formatterString . '};';
        }
        foreach ( $this->columnStyleFormatters as $columnField => $columnStyleFormat ) {
            $value = str_replace('"', '\\"', $columnStyleFormat);
            $addOnLoad .= $id . '.columnStyleFormatters["' . $columnField . '"] = "' . $value . '";';
        }
        $addOnLoad .= $id . '.prepareFormatters();';
        $addOnLoad .= '});';
        $script->addCode($addOnLoad);
    }

    /**
     * Pre render
     *
     * @return string
     */
    public function preRender() {
        $output = '';
        if ( $this->itemStore != null ) {
            $output .= $this->itemStore->preRender();
        }

        $output .= parent::preRender();
        
        // Auto generate on double click
        if ( $this->hasDojoAttribute('onRowDblClick') == false && $this->isOnDoubleClickAutoGenerate() ) {
            $actionSet = $this->getAutoGenerateActionSet();
            if ( $actionSet != null && $actionSet->hasAction('edit') ) {
                $this->setOnDoubleClick($actionSet->getId() . '.onButtonEdit();');
            }
        }

        // Register to dialog for update
        $dialog = $this->getParentWithType('Clevis_View_Dialog');
        if ( $dialog != null) {
            $dialog->addUpdateItem($this);
        }

        // Pre render context menu
        $actionSet = $this->getAutoGenerateActionSet();
        if ( $actionSet != null ) {
            $this->contextMenu->setAutoGenerateActionSet($actionSet);
            $this->contextMenu->preRender();
        }
        
        // Context menu
        if ( $this->hasContextMenu() ) {
            $this->setDojoAttribute('contextMenu', $this->contextMenu->getId() );
        }

        return $output;
    }

    /**
     * Render header menu
     *
     * @return string
     */
    public function renderHeaderMenu() {
        $output = '<div dojoType="clevis.view.ContextMenu" jsId="' . $this->getId() . '_headerMenu" id="' . $this->getId() . '_headerMenu" style="display: none;">' . PHP_EOL;
        $output .= '    <div dojoType="dojox.widget.PlaceholderMenuItem" label="GridColumns""></div>' . PHP_EOL;
        $output .= '    <div dojoType="dijit.MenuItem" onClick="' . $this->getId() . '.restoreColumnsSize()">Výchozí velikost sloupců</div>' . PHP_EOL;
        $output .= '    <div dojoType="dijit.MenuItem" onClick="' . $this->getId() . '.restoreColumnsOrder()">Výchozí pořadí sloupců</div>' . PHP_EOL;
        $output .= '    <div dojoType="dijit.MenuSeparator"></div>' . PHP_EOL;
        $output .= '    <div dojoType="dijit.MenuItem" onClick="' . $this->getId() . '.refreshContent()">Obnovit obsah</div>' . PHP_EOL;
        $output .= '</div>' . PHP_EOL;
        return $output;
    }

    /**
     * Render grid
     *
     * @return string
     */
    public function render() {
        $attributes = array();
        $attributes['onResizeColumn'] = $this->getId() . '.onSettingsChanged()';
        if ( $this->hasRowsPerPage() )
            $attributes['rowsPerPage'] = $this->getRowsPerPage();
        if ( $this->getColumnReordering() )
            $attributes['columnReordering'] = true;
        if ( $this->getColumnHiding() )
            $attributes['columnHiding'] = true;
        if ( $this->columnSort != null ) {
            $index = 0;
            foreach ( $this->items as $key => $item ) {
                if ( $item->getField() == $this->columnSort ) {
                    $index = $key + 1;
                    break;
                }
            }
            if ( $index != 0 )
                $this->setDojoAttribute('sortInfo', $index);
        }

        $output = '';
        $output .= '<!-- GRID BEGIN [' . $this->getName() . '] -->' . PHP_EOL;
        $output .= $this->renderScript();
        $output .= $this->renderHeaderMenu();

        if ( $this->itemStore != null )
            $output .= $this->itemStore->render();

        if ( $this->hasContextMenu() )
            $output .= $this->contextMenu->render();

        if ( $this->columnAutoSize ) {
            // Set first text column as auto width
            $columnAuto = null;
            foreach ( $this->items as $column ) {
                if ( $column->getWidth() == 'auto' ) {
                    $columnAuto = null;
                    break;
                } else if ( $columnAuto == null && (($column->getType() & COLUMN_TEXT) == COLUMN_TEXT || ($column->getType() & COLUMN_ENTITY) == COLUMN_ENTITY) ) {
                    $columnAuto = $column;
                }
            }
            if ( $columnAuto != null ) {
                $columnAuto->setWidth('auto');
            }
        }
        
        $output .= $this->renderBeginTag('table',$attributes);
        $output .= '<thead>' . PHP_EOL;
        $output .= '  <tr>' . PHP_EOL;
        $output .= $this->renderContent();
        $output .= '  </tr>' . PHP_EOL;
        $output .= '</thead>' . PHP_EOL;
        $output .= $this->renderEndTag();
        $output .= '<!-- GRID END [' . $this->getName() . '] -->' . PHP_EOL;

        return $output;
    }

