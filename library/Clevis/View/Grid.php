     * @param $columnField
     * @return void
     */
    public function setColumnSort($columnField) {
        $this->columnSort = $columnField;
    }

    /**
     * Add column to grid
     *
     * @param string $type
     * @param string $field
     * @param string $title
     * @param int $width
     * @return Clevis_View_GridColumn
     */
    public function addColumn($type, $field, $title, $width = null) {
        // Prepare parameters
        $align = ALIGN_LEFT;
        $format = null;

        // Setup parameters based on type
        if ( ($type & COLUMN_ID) == COLUMN_ID ) {
            if ( Clevis_Helper::isDevelopmentEnvironment() == false )
                return null;
            if ( $width == null )
                $width = 30;
        } else if ( ($type & COLUMN_BOOL) == COLUMN_BOOL ) {
            $align = ALIGN_CENTER;
            $format = Clevis_View_Grid::FORMATTER_BOOL;
            if ( $width == null )
                $width = 30;
        } else if ( ($type & COLUMN_NUMBER) == COLUMN_NUMBER ) {
            $align = ALIGN_RIGHT;
            if ( $width == null )
                $width = 15;
        } else if ( ($type & COLUMN_CURRENCY) == COLUMN_CURRENCY ) {
            $align = ALIGN_RIGHT;
            $format = Clevis_View_Grid::FORMATTER_CURRENCY;
        } else if ( ($type & COLUMN_DATE) == COLUMN_DATE ) {
            $format = Clevis_View_Grid::FORMATTER_DATE;
            if ( $width == null )
                $width = 75;
        } else if ( ($type & COLUMN_TIME) == COLUMN_TIME ) {
            $align = ALIGN_CENTER;
            if ( $width == null )
                $width = 60;
            $format = Clevis_View_Grid::FORMATTER_TIME;
        } else if ( ($type & COLUMN_COUNTER) == COLUMN_COUNTER ) {
            $align = ALIGN_RIGHT;
            $format = Clevis_View_Grid::FORMATTER_COUNTER;
            if ( $width == null )
                $width = 30;
        }

        // Change width if null
        if ( $width == null)
            $width = 100;
        
        $column = new Clevis_View_GridColumn();
        $column->setType($type);
        $column->setField($field);
        $column->setTitle($title);
        $column->setWidth($width);
        $column->setAlign($align);
        if ( $format != null )
            $this->setColumnFormat($field, $format);
        $column = $this->addItem($column);

        if ( ($type & COLUMN_CHECKBOX) == COLUMN_CHECKBOX )
            $column->setCheckBox(true);
        else if ( ($type & COLUMN_EDITBOX) == COLUMN_EDITBOX )
            $column->setEditBox(true);
        else if ( ($type & COLUMN_COMBOBOX) == COLUMN_COMBOBOX )
            $column->setComboBox(true);
        
        return $column;
    }
    
    /**
     * Set column format
     *
     * @param string $columnField
     * @param $columnFormat
     * @return void
     */
    public function setColumnFormat($columnField, $columnFormat) {
        if ( $columnFormat instanceof Clevis_View_Grid_Formatter ) {
            $this->columnFormatters[$columnField] = $columnFormat;
        } else {
            if ( $columnFormat == self::FORMATTER_DATE )
                $columnFormat = new Clevis_View_Grid_FormatterDate();
            else if ( $columnFormat == self::FORMATTER_TIME )
                $columnFormat = new Clevis_View_Grid_FormatterTime();
            else if ( $columnFormat == self::FORMATTER_BOOL )
                $columnFormat = new Clevis_View_Grid_FormatterBool();
            else if ( $columnFormat == self::FORMATTER_CURRENCY )
                $columnFormat = new Clevis_View_Grid_FormatterCurrency();
            else if ( $columnFormat == self::FORMATTER_COUNTER )
                $columnFormat = new Clevis_View_Grid_FormatterCounter();
            else
                throw Exception('Unknown grid column format [' . $columnFormat . ']!');
            $this->columnFormatters[$columnField] = $columnFormat;
        }
    }

    /**
     * Set column style formatter
     *
     * @param string $columnField
     * @param string $columnStyleFormat
     * @return void
     */
    public function setColumnStyle($columnField, $columnStyleFormat) {
        $this->columnStyleFormatters[$columnField] = $columnStyleFormat;
    }

    /**
     * Add context menu item
     *
     * @param string $title
     * @param string $code
     */
    public function addContextMenuItem($title,$code,$icon = null) {
        $this->contextMenu->addContextMenuItem($title,$code,$icon);
    }

