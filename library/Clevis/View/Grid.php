class Clevis_View_Grid extends Clevis_View_ItemElementScript implements Clevis_View_CurrentValueAble, Clevis_View_ItemHasStore {
    
    /** Formatters */
    const FORMATTER_DATE     = 'date';
    const FORMATTER_TIME     = 'time';
    const FORMATTER_BOOL     = 'bool';
    const FORMATTER_CURRENCY = 'currency';
    const FORMATTER_ENUM     = 'enum';
    const FORMATTER_ENTITY   = 'entity';
    const FORMATTER_CUSTOM   = 'custom';
    const FORMATTER_COUNTER  = 'counter';
    
    /**
     * Manages if on double click is auto generate
     *
     * @var boolean
     */
    private $onDoubleClickAutoGenerate = true;

    /**
     * ActionSet for auto generate
     *
     * @var Clevis_View_Grid
     */
    private $autoGenerateActionSet;

    /**
     * Auto size first text column (or column with width "auto")
     * @var bool
     */
    private $columnAutoSize = false;

    /**
     * Rows per page for data loading
     * 
     * @var integer
     */
    private $rowsPerPage;

    /**
     * Store of grid
     * 
     * @var Clevis_View_Store
     */
    private $store;

    /**
     * Store of grid inside grid (should be rendered)
     *
     * @var Clevis_View_Store
     */
    public $itemStore;

    /**
     * Allow reorder grid columns
     *
     * @var boolean
     */
    private $columnReordering = true;

    /**
     * Allow grid columns hiding
     *
     * @var boolean
     */
    private $columnHiding = true;

    /**
     * Default column sorting
     *
     * @var string
     */
    private $columnSort = null;
    
    /**
     * Column formatters
     * 
     * @var array          
     */         
    private $columnFormatters = array();

    /**
     * Column style formatters
     *
     * @var array
     */
    private $columnStyleFormatters = array();

    /**
     * Context menu
     *
     * @var array
     */
    private $contextMenu = null;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setType('clevis.view.Grid');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('grid');
        $this->setClass('clevis-view-grid');
        $this->setDojoAttribute('clientSort', true);
        $this->setPersistSettings(true);
        $this->setStyleFullSize();
        $this->setStyleMinSize(50,50);
        // Create context menu
        $this->contextMenu = new Clevis_View_ContextMenu();
        $this->contextMenu->setParent($this);
    }

    /**
     * Set on double click auto generate
     *
     * @param boolean $onDoubleClickAutoGenerate
     * @return void
     */
    public function setOnDoubleClickAutoGenerate($onDoubleClickAutoGenerate) {
        $this->onDoubleClickAutoGenerate = $onDoubleClickAutoGenerate;
    }

    /**
     * Is on double click auto generate
     *
     * @return boolean $onDoubleClickAutoGenerate
     */
    public function isOnDoubleClickAutoGenerate() {
       return $this->onDoubleClickAutoGenerate;
    }

    /**
     * Set action set for on auto generate
     *
     * @param Clevis_View_ActionSet $autoGenerateActionSet
     * @return void
     */
    public function setAutoGenerateActionSet(Clevis_View_ActionSet & $autoGenerateActionSet) {
        $this->autoGenerateActionSet = $autoGenerateActionSet;
    }

    /**
     * Get action set for auto generate
     *
     * @return Clevis_View_ActionSet $autoGenerateActionSet
     */
    public function & getAutoGenerateActionSet() {
        if ( $this->autoGenerateActionSet == null ) {
            $this->autoGenerateActionSet = Clevis_View_ActionSet::findActionSetForAutoGenerate($this);
        }
        return $this->autoGenerateActionSet;
    }

    /**
     * Auto size first text column (or column with width "auto")
     *
     * @var bool
     */
    public function setColumnAutoSize($columnAutoSize) {
        $this->columnAutoSize = $columnAutoSize;
    }

    /**
     * Set rows per page for data loading
     *
     * @param $rowsPerPage
     * @return void
     */
    public function setRowsPerPage($rowsPerPage) {
        $this->rowsPerPage = $rowsPerPage;
    }

    /**
     * Het rows per page for data loading
     *
     * @return boolean
     */
    public function hasRowsPerPage() {
        return $this->rowsPerPage != null;
    }

    /**
     * Get rows per page for data loading
     *
     * @return integer $rowsPerPage
     */
    public function getRowsPerPage() {
        return $this->rowsPerPage;
    }

    /**
     * Set store
     *
     * @param Clevis_View_Store $store
     * @return void
     */
    public function setStore(Clevis_View_Store & $store) {
        if ( $this->store != null )
            $store->removeConnectedItem($this);
        $this->store = &$store;
        if ( $store != null )
            $store->addConnectedItem($this);
    }

    /**
     * Setup listing store
     *
     * @param array $url
     * @param enum $mode (STORE_MODE_WRITE | STORE_MODE_READ)
     * @return Clevis_View_Store
     */
    public function setupStore($url, $mode = STORE_MODE_WRITE) {
        assert($mode == STORE_MODE_READ || $mode == STORE_MODE_WRITE);
        $store = new Clevis_View_Store();
        if ( $mode == STORE_MODE_WRITE ) {
            $store->setDynamic(true);
            $store->setReadOnly(false);
        } else {
            $store->setDynamic(false);
            $store->setReadOnly(true);
        }
        $store->setUrl($url);
        $store->setName($this->getName());
        $this->setStore($store);

        // Set as item
        $this->itemStore = $store;
        $this->itemStore->setParent($this);

        return $store;
    }

    /**
     * Setup listing store to parent data
     *
     * @param string $data Parent data attribute
     * @param enum $mode (STORE_MODE_WRITE | STORE_MODE_READ)
     * @return Clevis_View_Store
     */
    public function setupStoreData($data, $mode = STORE_MODE_WRITE) {
        assert($mode == STORE_MODE_READ || $mode == STORE_MODE_WRITE);
        $store = new Clevis_View_Store();
        $store->setDynamic(false);
        if ( $mode == STORE_MODE_WRITE ) {
            $store->setReadOnly(false);
        } else {
            $store->setReadOnly(true);
        }
        $store->registerToParentData($data);
        $store->setName($this->getName());
        $this->setStore($store);

        // Set as item
        $this->itemStore = $store;
        $this->itemStore->setParent($this);

        return $store;
    }

    /**
     * Setup grid store dialog
     *
     * @param Clevis_View_Dialog|string $dialog  Dialog for entity creation and editation (or its id)
     * @param array $defaultValues  Default values for entity creation and editation
     * @param string|null $url  Url for entity creation, editation and deletation, when is null use url from store
     * @param array|null $values  Entity values to determine which dialog to use (default mode is only one dialog so entity values can be null)
     * @param boolean $addItem  Flag if $dialog should by added by addItem to render tree
     * @return Clevis_View_Dialog
     */
    public function setupStoreDialog($dialog, $defaultValues = array(), $url = null, $values = null, $addItem = true) {
        if ( $this->store == null ) {
            throw new Exception("Store must not be empty!");
        }
        $this->store->getEntity()->addDialog($dialog, $defaultValues, $url, $values, $addItem);
        return $dialog;
    }

    /**
     * Has store
     * 
     * @return boolean
     */
    public function hasStore() {
        return $this->store != null;
    }

    /**
     * Get store of grid
     *
     * @return Clevis_View_Store $store
     */
    public function & getStore() {
        return $this->store;
    }

