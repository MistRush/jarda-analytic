     * Check if grid has context menu
     *
     * @return boolean
     */
    public function hasContextMenu() {
        return $this->contextMenu->getItemCount() > 0;
    }

    /**
     * Bind grid to dynamic param
     * 
     * @param string $dynamicParam
     */
    public function bindDynamicParam($dynamicParam) {
        Clevis_DynamicUrl::bindDynamicParam($this, $dynamicParam);
    }

