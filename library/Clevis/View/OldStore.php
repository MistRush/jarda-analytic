<?php

/**
 * Class that represents store
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_OldStore extends Clevis_View_ItemElementScriptType {

    /**
     * URL
     *
     * @var string
     */
    private $url;

    /**
     * URL filter
     *
     * @var string
     */
    private $urlFilter;

    /**
     * URL default action ('data-list' for example)
     *
     * @var string
     */
    private $urlDefaultAction;

    /**
     * Connected items to this stores
     *
     * @var array of Clevis_View_ItemElement
     */
    private $connectedItems = array();

    /**
     * Array of parent stores
     *
     * @var array
     */
    private $storeParents = array();
    
    /**
     * Children stores
     *
     * @var array
     */
    private $storeChildren = array();

    /**
     * Data items
     * 
     * @var array
     */
    private $dataItems = array();

    /**
     * Filter items
     *
     * @var array
     */
    private $filterItems = array();

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('store');
        $this->setBaseType('dojox/data/QueryReadStore');
        // Store is always data item (it will connect to first data parent)
        $this->dataSetIsItem(true);
        $this->setClass('clevis-hidden');
    }

    /**
     * Set url
     *
     * @param string $url
     */
    public function setUrl($url) {
        $this->url = $url;
    }

    /**
     * Set url filter
     *
     * @param string $urlFilter
     */
    public function setUrlFilter($urlFilter = null) {
        $this->urlFilter = $urlFilter;
    }

    /**
     *  Set url default action
     *
     * $param string $urlDefaultAction
     */
    protected function setUrlDefaultAction($urlDefaultAction) {
        $this->urlDefaultAction = $urlDefaultAction;
    }

    /**
     * Has url
     *
     * @return boolean
     */
    public function hasUrl() {
        return $this->url != null;
    }

    /**
     * Get url
     *
     * @return string $url
     */
    public function getUrl() {
        return $this->url;
    }

    /**
     * Register to parent store
     * 
     * @param Clevis_View_Store $storeParent
     * @param array $storeQuery for example array('User_ID' => 'parent.ID')
     */
    public function registerToParentStore(Clevis_View_OldStore $storeParent, $storeQuery) {
        $this->storeParents[] = array('store' => $storeParent, 'query' => $storeQuery);
        $storeParent->storeChildren[] = $this;
    }

    /**
     * Add item that is connected to store and need to be refreshed
     */
    public function addConnectedItem(Clevis_View_ItemElement & $connectedItem) {
        $this->connectedItems[] = $connectedItem;
    }

    /**
     * Remove connected item
     */
    public function removeConnectedItem(Clevis_View_ItemElement & $connectedItem) {
        array_remove_reference($this->connectedItems,$connectedItem);
    }

    /**
     * Add data item to store
     * 
     * @param array $dataItem
     */
    public function addDataItem($dataItem) {
        $this->dataItems[] = $dataItem;
    }

    /**
     * Add filter item to store
     *
     * @param Clevis_View_Form_Item $filterItem
     */
    public function addFilterItem(Clevis_View_Form_Item $filterItem) {
        $this->filterItems[] = $filterItem;
    }

    /**
     * Register this store to parent dialog data if some exists
     *
     * @param string $dataAttribute
     */
    public function registerToParentDataForCurrentItem($dataAttributeForCurrentItem) {
        $this->setDojoAttribute('dataAttributeForCurrentItem', $dataAttributeForCurrentItem);
    }

    /**
     * On init script type notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptType(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptType($scriptType);
        $scriptType->addMethod('initConnectedItems');
        $scriptType->addMethod('initChildStores');
        $scriptType->addMethod('fillDataItems',array('data'));
        $scriptType->addMethod('create',array('data'));
    }

    /**
     * On init script type methods notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptTypeMethods(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptTypeMethods($scriptType);

        $initConnectedItemsBody = '';
        foreach ( $this->connectedItems as $connectedItem ) {
            if ( empty($initConnectedItemsBody) == false )
                $initConnectedItemsBody .= PHP_EOL;
            $initConnectedItemsBody .= 'this.connectedItems.push("' . $connectedItem->getId() . '");';
        }
        $scriptType->provideMethodBody('initConnectedItems', $initConnectedItemsBody);

        $initFilterItemsBody = '';
        foreach ( $this->filterItems as $filterItem ) {
            if ( empty($initFilterItemsBody) == false )
                $initFilterItemsBody .= PHP_EOL;
            $initFilterItemsBody .= 'this.filterItems.push("' . $filterItem->getId() . '");';
        }
        $scriptType->provideMethodBody('initFilterItems', $initFilterItemsBody);

        $initParentStoresBody = '';
        foreach ( $this->storeParents as $parentStore ) {
            if ( empty($initParentStoresBody) == false )
                $initParentStoresBody .= PHP_EOL;
            $initParentStoresBody .= 'this.parentStores.push({getStore: function(){ return ' . $parentStore['store']->getId() . ';}, query: ' . json_encode($parentStore['query']) . '});' . PHP_EOL;
        }
        $scriptType->provideMethodBody('initParentStores', $initParentStoresBody);
        
        $initChildStoresBody = '';
        foreach ( $this->storeChildren as $childStore ) {
            if ( empty($initChildStoresBody) == false )
                $initChildStoresBody .= PHP_EOL;
            $initChildStoresBody .= 'this.childStores.push({getStore: function(){ return ' . $childStore->getId() . ';}});' . PHP_EOL;
        }
        $scriptType->provideMethodBody('initChildStores', $initChildStoresBody);

        $fillDataItemsBody = 'this.fillDataItemsPosition = 0;';
        foreach ( $this->dataItems as $dataItem ) {
            $fillDataItemsBody .= PHP_EOL . 'this.addDataItem(data,' . json_encode($dataItem) . ');';
        }
        $scriptType->provideMethodBody('fillDataItems',$fillDataItemsBody);

        $scriptType->provideMethodBody('create','return new ' . $this->getType() . '(data);');
    }

    /**
     * Pre render store
     *
     * @return string
     */
    public function preRender() {
        if ( $this->url != null ) {
            // Probably some parent dialog is added to page twice, check it!
            assert(is_array($this->url));

            // Prepare url filter
            if ( $this->urlFilter == null ) {
                $this->urlFilter = array();
                foreach ( $this->url as $key => $value ) {
                    if ( $key == 'action' || $key == 'module' || $key == 'controller' )
                        continue;
                    $this->urlFilter[$key] = $value;
                }
            }
            // Set default action for url
            if ( array_key_exists('action',$this->url) == false ) {
                if ( $this->urlDefaultAction != null )
                    $this->url['action'] = $this->urlDefaultAction;
            }
            // Prepare url
            $this->url = Clevis_Helper::formatUrl($this->url, false);
            // Set url and url filter as attribute
            $this->setDojoAttribute('url', $this->url);
            $this->setDojoAttribute('urlFilter', $this->urlFilter);
        }

        $output = parent::preRender();
        
        if ( $this->hasUrl() == false ) {
            $this->setDojoAttribute('data', array('items' => array()));
        }

        return $output;
    }

}
?>