<?php

/**
 * Class that represents Input TextBox
 *
 * @author Pavel Cihlar
 */
class Clevis_View_Form_Item extends Clevis_View_ItemElementScript {

    /**
     * Form item label
     *
     * @var string
     */
    private $label;
    
    
    /**
     * Form item after text
     * 
     * @var string
     */
    private $afterText;

    /**
     * Form item column span
     *
     * @var number
     */
    private $columnSpan;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->enableScript(false);
        $this->setClass('clevis-view-form-item');
        $this->setIdAutoGenerated(true);
        $this->setHelpIdRequired(true);
        $this->setDojoAttribute('invalidMessage','');
    }

    /**
     * Set label
     * 
     * @param string $label
     * @return void
     */
    public function setLabel($label) {
        $this->label = $label;
    }

    /**
     * Get label
     *
     * @return string $label
     */
    public function getLabel() {
        return $this->label;
    }

    /**
     * Set after text
     * 
     * @param string $afterText
     * @return void
     */
    public function setAfterText($afterText) {
        $this->afterText = $afterText;
    }

    /**
     * Get after text
     *
     * @return string
     */
    public function getAfterText() {
        return $this->afterText;
    }

    /**
     * Set column spand
     *
     * @param string $columnSpan
     * @return void
     */
    public function setColumnSpan($columnSpan) {
        $this->columnSpan = $columnSpan;
    }

    /**
     * Get column span
     *
     * @return string $columnSpan
     */
    public function getColumnSpan() {
        return $this->columnSpan;
    }

}
?>