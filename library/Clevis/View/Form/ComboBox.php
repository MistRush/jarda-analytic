<?php

/**
 * Class that represents combo box
 *
 * @author Pavel Cihlar
 */

class Clevis_View_Form_ComboBox extends Clevis_View_Form_Item implements Clevis_View_ItemHasStore {

    const TYPE_NORMAL = 'normal';
    const TYPE_FILTERING = 'filtering';

    /**
     * Store of combo box
     * 
     * @var Clevis_View_Store
     */
    private $store;

    /**
     * Items array for combo box
     *
     * @var array of strings
     */
    private $dataItems = array();

    /**
     * Type
     *
     * @var string
     */
    private $type;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setRequired(false);
        $this->setIdAutoGenerated(true);
        $this->setType(self::TYPE_NORMAL);
    }

    /**
     * Set combo box type
     * 
     * @param $type (TYPE_NORMAL | TYPE_FILTERING)
     * @return void
     */
    public function setType($type) {
        $this->type = $type;
    }

    /**
     * Set store
     *
     * @param Clevis_View_Store store
     * @param string storeField
     * @return void
     */
    public function setStore($store, $storeField) {
        if ( $this->store != null )
            $store->removeConnectedItem($this);
        $this->store = $store;
        if ( $store != null )
            $store->addConnectedItem($this);
        $this->setDojoAttribute('searchAttr', $storeField);
    }

    /**
     * Has store
     * 
     * @return boolean
     */
    public function hasStore() {
        return $this->store != null;
    }

    /**
     * Get store of filtering select
     *
     * @return Clevis_View_Store $store
     */
    public function & getStore() {
        return $this->store;
    }

    /**
     * Set required
     *
     * @param string $required
     * @return void
     */
    public function setRequired($required) {
        $this->setDojoAttribute('required', $required);
    }

    /**
     * Get required
     *
     * @return string $required
     */
    public function getRequired() {
        return $this->getAttribute('required');
    }

    /**
     * Set readOnly
     *
     * @param string $readOnly
     * @return void
     */
    public function setReadOnly($readOnly) {
        $this->setElementAttribute('readonly', $readOnly);
        $this->setStyle('background-color', '#eee');
    }

    /**
     * Get readOnly
     *
     * @return string $readOnly
     */
    public function getReadOnly() {
        return $this->getAttribute('readonly');
    }

    /**
     * Set combo box editable additional value. For example if you want edit by combo "Person_ID" and you want
     * allow to user to set custom value into "Person" column
     * 
     * @param $editable  Flag if editable is enabled
     * @param $editableName  Custom column name
     * @return void
     */
    public function setEditable($editable, $editableName = null) {
        if ( $editable ) {
            if ( $editableName == null )
                $editableName = $this->getName();
            $this->setType(self::TYPE_FILTERING);
            $this->setDojoAttribute('editableName', $editableName);
        } else {
            $this->setType(self::TYPE_NORMAL);
            $this->setDojoAttribute('editableName', null);
        }
    }

    /**
     * Add item
     *
     * @param $value
     * @param $name
     * @return void
     */
    public function addDataItem($value, $name) {
        $this->dataItems[$value] = $name;
    }

    /**
     * Add items
     *
     * @param array $items
     * @return void
     */
    public function addDataItems($items) {
        if ( array_values($items) === $items ) {
            foreach ( $items as $value )
                $this->addDataItem($value, $value);
        } else {
            foreach ( $items as $value => $name)
                $this->addDataItem($value, $name);
        }
    }

    /**
     * Pre render
     * 
     * @return string
     */
    public function preRender() {
        // Set type
        if ( $this->type == self::TYPE_FILTERING ) {
            $this->addClass('clevis-view-comboBox');
            parent::setType('clevis.view.form.ComboBox');
        } else if ( $this->type == self::TYPE_NORMAL ) {
            $this->addClass('clevis-view-comboBoxList');
            parent::setType('clevis.view.form.ComboBoxList');
        } else {
            assert(false);
        }
        return parent::preRender();
    }
    
    /**
     * Render filtering select
     *
     * @return string
     */
    public function render() {
        $output = '';
        $output .= $this->renderScript();

        if ( ($this->getParent() instanceof Clevis_View_Form) == false )
            $output .= '<b>' . $this->getLabel() . '</b>';

        if ( $this->hasStore() ) {
            $this->setTag('input', true);
            $output .= $this->renderBeginTag();
        } else {
            $output .= $this->renderBeginTag('select');
            foreach ( $this->dataItems as $value => $name ) {
                $output .= '<option value="' . $value . '">' . $name . '</option>';
            }
            $output .= $this->renderEndTag();
        }
        return $output;
    }

}
?>