<?php

/**
 * Class that represents combo box
 *
 * @author Pavel Cihlar
 */

class Clevis_View_Form_Select extends Clevis_View_Form_Item {


    /**
     * Items array for combo box
     *
     * @var array of strings
     */
    private $dataItems = array();



    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setRequired(false);
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('select');
        $this->setType('clevis.view.form.ComboBoxList');
        $this->addClass('clevis-view-comboBoxList');
        $this->setDojoAttribute('maxHeight','280');
    }

    /**
     * Set required
     *
     * @param string $required
     * @return void
     */
    public function setRequired($required) {
        $this->setDojoAttribute('required',$required);
    }

    /**
     * Get required
     *
     * @return string $required
     */
    public function getRequired() {
        return $this->getAttribute('required');
    }

    /**
     * Set readOnly
     *
     * @param string $readOnly
     * @return void
     */
    public function setReadOnly($readOnly) {
        $this->setElementAttribute('readonly', $readOnly);
        $this->setStyle('background-color', '#eee');
    }

    /**
     * Get readOnly
     *
     * @return string $readOnly
     */
    public function getReadOnly() {
        return $this->getAttribute('readonly');
    }

    /**
     * Add item
     *
     * @param $value
     * @param $name
     * @return void
     */
    public function addDataItem($value, $name) {
        $this->dataItems[$value] = $name;
    }

    /**
     * Add items
     *
     * @param array $items
     * @return void
     */
    public function addDataItems($items) {
        if ( array_values($items) === $items ) {
            foreach ( $items as $value )
                $this->addDataItem($value, $value);
        } else {
            foreach ( $items as $value => $name)
                $this->addDataItem($value, $name);
        }
    }

    
    /**
     * Render filtering select
     *
     * @return string
     */
    public function render() {
        $output = '';
        $output .= $this->renderScript();

        if ( ($this->getParent() instanceof Clevis_View_Form) == false )
            $output .= '<b>' . $this->getLabel() . '</b>';


            $output .= $this->renderBeginTag('div');            
            foreach ( $this->dataItems as $value => $name ) {

                $disabled = '';
                $valueParts = explode("-", $value);
                if ( $valueParts[0] == 'd' ) $disabled = 'disabled="true" ';

                $output .= '<span ' . $disabled . ' value="' . $value . '">' . $name . '</span>';
            }
            $output .= $this->renderEndTag();

        return $output;
    }

}
?>