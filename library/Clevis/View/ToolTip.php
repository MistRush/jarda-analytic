<?php

/**
 * Class that represents ToolTip
 *
 * @author Pavel Cihlar
 */
class Clevis_View_ToolTip extends Clevis_View_ItemElement {

    /**
     * ToolTip content
     *
     * @var string
     */
    private $content;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setType('clevis.view.Tooltip');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('toolTip');
    }

    /**
     * Set content
     * 
     * @param string $content
     * @return void
     */
    public function setContent($content) {
        $this->content = $content;
    }

    /**
     * Get content
     *
     * @return string $content
     */
    public function getContent() {
        return $this->content;
    }

    /**
     * Set connectId
     *
     * @param string $connectId
     * @return void
     */
    public function setConnectId($connectId) {
        $this->setDojoAttribute('connectId',$connectId);
    }

    /**
     * Get connectId
     *
     * @return string $connectId
     */
    public function getConnectId() {
        return $this->getAttribute('connectId');
    }

    /**
     * Set position
     *
     * @param string $position
     * @return void
     */
    public function setPosition($position) {
        $this->setDojoAttribute('position', array($position));
    }

    /**
     * Get position
     *
     * @return string $position
     */
    public function getPosition() {
        return $this->getAttribute('position');
    }

    /**
     * Render
     *
     * @return string
     */
    public function render() {
        $output = '';
        $output .= $this->renderBeginTag();
        $output .= $this->getContent();
        $output .= $this->renderEndTag();

        return $output;
    }
}
?>