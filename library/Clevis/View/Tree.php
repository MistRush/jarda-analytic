<?php

/**
 * Class that represents tree control
 *
 * @author Pavel Cihlar
 */
class Clevis_View_Tree extends Clevis_View_ItemElementScriptType implements Clevis_View_CurrentValueAble, Clevis_View_ItemHasStore {

    /**
     * Store of grid
     *
     * @var Clevis_View_Store
     */
    private $store;

    /**
     * Manages if on double click is auto generate
     *
     * @var boolean
     */
    private $onDoubleClickAutoGenerate = true;

    /**
     * ActionSet for auto generate
     *
     * @var Clevis_View_Grid
     */
    private $autoGenerateActionSet;

    /**
     * On double click body
     *
     * @var string
     */
    private $onDoubleClick = null;

    /**
     * Context menu
     *
     * @var array
     */
    private $contextMenu = null;

    /**
     * Constructor
     */
    public function __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setBaseType('clevis.view.Tree');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('tree');
        $this->setStyleFullSize();
        $this->setStyleMinSize(100, 200);
        // Create context menu
        $this->contextMenu = new Clevis_View_ContextMenu();
        $this->contextMenu->setParent($this);
    }

    /**
     * Set on double click auto generate
     *
     * @param boolean $onDoubleClickAutoGenerate
     * @return void
     */
    public function setOnDoubleClickAutoGenerate($onDoubleClickAutoGenerate) {
        $this->onDoubleClickAutoGenerate = $onDoubleClickAutoGenerate;
    }

    /**
     * Is on double click auto generate
     *
     * @return boolean $onDoubleClickAutoGenerate
     */
    public function isOnDoubleClickAutoGenerate() {
        return $this->onDoubleClickAutoGenerate;
    }

    /**
     * Set action set for on auto generate
     *
     * @param Clevis_View_ActionSet $autoGenerateActionSet
     * @return void
     */
    public function setAutoGenerateActionSet(Clevis_View_ActionSet & $autoGenerateActionSet) {
        $this->autoGenerateActionSet = $autoGenerateActionSet;
    }

    /**
     * Get action set for auto generate
     *
     * @return Clevis_View_ActionSet $autoGenerateActionSet
     */
    public function & getAutoGenerateActionSet() {
        if ($this->autoGenerateActionSet == null) {
            $this->autoGenerateActionSet = Clevis_View_ActionSet::findActionSetForAutoGenerate($this);
        }
        return $this->autoGenerateActionSet;
    }

    /**
     * Set store
     *
     * @param Clevis_View_Store $store
     * @return void
     */
    public function setStore(Clevis_View_OldStore & $store) {
        if ($this->store != null)
            $store->removeConnectedItem($this);
        $this->store = &$store;
        if ($store != null)
            $store->addConnectedItem($this);
    }

    /**
     * Has store
     *
     * @return boolean
     */
    public function hasStore() {
        return $this->store != null;
    }

    /**
     * Get store of tree
     *
     * @return Clevis_View_Store $store
     */
    public function & getStore() {
        return $this->store;
    }

    /**
     * Set root item label
     *
     * @param string $rootLabel
     */
    public function setRootLabel($rootLabel) {
        $this->setDojoAttribute('rootLabel', $rootLabel);
    }

    /**
     * Setup tree items
     *
     * @param string $itemLabel
     */
    public function setupTreeItem($itemLabel, $itemParentIdentifier = null) {
        $this->setDojoAttribute('itemLabel', $itemLabel);
        if ($itemParentIdentifier != null)
            $this->setDojoAttribute('itemParentIdentifier', $itemParentIdentifier);
    }

    /**
     * Set on double click body
     *
     * @param string $body
     */
    public function setOnDoubleClick($body) {
        $this->onDoubleClick = $body;
    }

    /**
     * Add context menu item
     *
     * @param string $name
     * @param string $title
     * @param string $icon
     * @param string $body
     */
    public function addContextMenuItem($name, $title, $icon = null, $body = null) {
        $this->contextMenu->addContextMenuItem($name, $title, $icon, $body);
    }

    /**
     * Check if tree has context menu
     *
     * @return boolean
     */
    public function hasContextMenu() {
        return $this->contextMenu->getItemCount() > 0;
    }

    /**
     * Get current value
     *
     * @return string
     */
    public function getCurrentValue() {
        return $this->getId() . '.getIdentifier()';
    }

    /**
     * On init script type methods notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptTypeMethods(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptTypeMethods($scriptType);

        if ($this->onDoubleClick != null) {
            $scriptType->addMethod('onDblClick', null, $this->onDoubleClick);
        }
    }

    /**
     * Pre render
     *
     * @return void
     */
    public function preRender() {
        // Auto generate on double click
        if ($this->hasAttribute('onRowDblClick') == false && $this->isOnDoubleClickAutoGenerate()) {
            $actionSet = $this->getAutoGenerateActionSet();
            if ($actionSet != null && $actionSet->hasAction('edit')) {
                $this->setOnDoubleClick($actionSet->getId() . '.onButtonEdit();');
            }
        }

        if ($this->store instanceof Clevis_View_StoreWriteQuery) {
            if ($this->hasAttribute('itemParentIdentifier') == false)
                throw new Exception('For StoreWriteQuery the item parent identifier must be set (by method setupTreeItem)!');
        }

        parent::preRender();

        // Pre render context menu
        $actionSet = $this->getAutoGenerateActionSet();
        if ($actionSet != null)
            $this->contextMenu->setAutoGenerateActionSet($actionSet);
        $this->contextMenu->preRender();
    }

    /**
     * Render tree
     *
     * @return string
     */
    public function render() {
        $output = '<div style=""width: ' . $this->getStyleWidth() . '; height: ' . $this->getStyleHeight() . ';">';

        if ($this->hasContextMenu()) {
            $this->setDojoAttribute('contextMenu', $this->contextMenu->getId());
            $output .= $this->contextMenu->render();
        }

        $output .= parent::render();
        $output .= '</div>';

        return $output;
    }

}

?>