<?php

/**
 * Class that represents dialog
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Dialog extends Clevis_View_Container {

    /**
     * Width of dialog
     * 
     * @var string
     */
    private $width;

    /**
     * Height of dialog
     *
     * @var string
     */
    private $height;

    /**
     * Write store that uses this dialog for editing operations
     *
     * @var Clevis_View_StoreWrite
     */
    private $storeWrite;

    /**
     * Flag if dialog is inline
     *
     * @var bool
     */
    private $inline = false;

    /**
     * Inline dialog size is auto
     * 
     * @var bool
     */
    private $inlineSizeAuto = true;

    /**
     * Items to update when dialog is showed
     *
     * @var array of items
     */
    private $updateItems = array();

    /**
     * Constructor
     */
    public function  __construct($title = null, $name = null) {
        parent::__construct();
        $this->setIdAutoGenerated(true);
        $this->setHelpIdRequired(true);
        $this->setItemRoot(true);
        $this->setItemGroup('dialog');
        $this->enableScript(true);
        $this->enableScriptType(true);
        $this->setBaseType('clevis/view/Dialog');
        // Dialog is data parent (all data items will connect to it)
        $this->dataSetIsParent(true);
        
        // Set name
        if ( $name != null ) {
            $this->setName($name);
        }
        
        // Set title
        if ( $title != null ) {
            $this->setTitle($title);
        }
    }
    
    /**
     * Set title
     *
     * @param string $title
     * @return void
     */
    public function setTitle($title) {
        $this->setDojoAttribute('title',$title);
    }

    /**
     * Get title
     *
     * @return string $title
     */
    public function getTitle() {
        return $this->getDojoAttribute('title');
    }

    /**
     * Set width of dialog
     *
     * @param $width
     * @return void
     */
    public function setWidth($width) {
        if ( is_numeric($width) )
            $width = $width . 'px';
        $this->width = $width;
    }

    /**
     * Get width
     *
     * @return string $width
     */
    public function getWidth() {
        return $this->width;
    }

    /**
     * Set height of dialog
     *
     * @param $height
     * @return void
     */
    public function setHeight($height) {
        if ( is_numeric($height) )
            $height = $height . 'px';
        $this->height = $height;
    }

    /**
     * Get height
     *
     * @return string $height
     */
    public function getHeight() {
        return $this->height;
    }

    /**
     * Set size of dialog
     * 
     * @param $width
     * @param $height
     */
    public function setSize($width,$height) {
        $this->setWidth($width);
        $this->setHeight($height);
    }

    /**
     * Set to show dialog on load
     *
     * @param boolean $debugOnLoadShow
     */
    public function setDebugOnLoadShow($debugOnLoadShow) {
        if ( $debugOnLoadShow ) {
            $this->setDojoAttribute('debugOnLoadShow', true);
        }
        else
            $this->unsetAttribute('debugOnLoadShow');
    }

    /**
     * Get formsToValidation value
     *
     * @param array of Extensin_View_Form $formToValidation
     */
    public function getFormsToValidation() {
        return $this->formsToValidation;
    }

    /**
     * Set write store that uses this dialog for editing operations
     *
     * @param Clevis_View_StoreWrite $storeWrite
     * @return void
     */
    public function setStoreWrite(Clevis_View_StoreWrite & $storeWrite) {
        $this->storeWrite = &$storeWrite;
    }

    /**
     * Get write store that uses this dialog for editing operations
     *
     * @return Clevis_View_StoreWrite $storeWrite
     */
    public function & getStoreWrite() {
        return $this->storeWrite;
    }

    /**
     * Set dialog as inline
     * 
     * @param $inline
     * @return void
     */
    public function setInline($inline, $inlineSizeAuto = true) {
        $this->inline = $inline;
        $this->inlineSizeAuto = $inlineSizeAuto;
    }

    /**
     * Is inline
     * 
     * @return bool
     */
    public function isInline() {
        return $this->inline;
    }

    /**
     * Is inline size auto
     * 
     * @return bool
     */
    public function isInlineSizeAuto() {
        return $this->inlineSizeAuto;
    }

    /**
     * Add item to update when dialog is shown
     *
     * @param Clevis_View_Item $item
     * @return void
     */
    public function addUpdateItem(Clevis_View_Item & $item) {
        $this->updateItems[] = $item;
    }

    /**
     * Remove item to update when dialog is shown
     *
     * @param Clevis_View_Item $item
     * @return void
     */
    public function removeUpdateItem(Clevis_View_Item & $item) {
        array_remove_reference($this->updateItems,$item);
    }

    /**
     * On init script notification
     *
     * @param Clevis_View_Script $script
     */
    protected function onInitScript(Clevis_View_Script & $script) {
        parent::onInitScript($script);
    }

    /**
     * On init script type methods notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptType(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptType($scriptType);

        // Update init items
        $updateBody = '';
        foreach ( $this->updateItems as $item ) {
            if ( empty($updateBody) == false )
                $updateBody .= PHP_EOL;
            $updateBody .= 'this.updateItems.push(' . $item->getId() . ');';
        }
        $scriptType->addMethod('updateInitItems',null,$updateBody);
    }

    /**
     * Pre render
     * 
     * @return string
     */
    public function preRender() {
        if ( $this->isInline() ) {
            $this->setBaseType('clevis/view/DialogInline');
        }
        if ( $this->isInlineSizeAuto() ) {
            $this->setStyle('display', 'inline-block');
        }
        return parent::preRender();
    }

    /**
     * Render dialog
     *
     * @return string
     */   
    public function render() {
        $output = '';
        $output .= '<!-- DIALOG BEGIN [' . $this->getName() . '] -->' . PHP_EOL;
        $output .= $this->renderBeginTag('div');
        $output .= '<div id="' . $this->getId() . '_content" class="' . ($this->isInline() ? 'clevis-view-dialogInlineContent' : 'clevis-view-dialogContent') . '"';
        if ( $this->getWidth() != null || $this->getHeight() != null ) {
            $output .= ' style="';
            if ( $this->getWidth() != null ) {
                $output .= 'width:' . $this->getWidth() . ';';
            }
            if ( $this->getHeight() != null ) {
                $output .= 'height:' . $this->getHeight() . ';';
            }
            $output .= '"';
        }
        $output .= '>' . PHP_EOL;
        $output .= $this->renderScript();
        $output .= $this->renderContent();
        $output .= '</div>' . PHP_EOL;
        $output .= $this->renderEndTag();
        $output .= '<!-- DIALOG END [' . $this->getName() . '] -->' . PHP_EOL;
        return $output;
    }

}
?>