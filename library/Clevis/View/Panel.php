<?php

/**
 * Class that represents panel
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Panel extends Clevis_View_Container {

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setType('dijit.layout.ContentPane');
        $this->setBorder(false);
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('panel');
    }
    
    /**
     * Set border to panel
     * 
     * @param boolean $border Show border in panel
     */
    public function setBorder($border) {
        if ( $border ) {
            $this->setClass('clevis-view-panel-border');
        } else {
            $this->setClass('clevis-view-panel');
        }
    }

}
?>