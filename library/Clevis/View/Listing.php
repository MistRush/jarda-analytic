<?php

/**
 * Class that represents data listing
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Listing extends Clevis_View_ItemElement {
    
    /** 
     * Layout 
     * 
     * @var Clevis_View_Layout
     */
    private $layout;
    
    /** Data listing title */
    private $title = null;
    
    /** Data listing filter set */
    private $filterSet = null;
    
    /** Filter default */
    private $filterDefault = FILTER_ENABLE;

    /**
     * Data listing store
     *
     * @var Clevis_View_Store
     */
    private $store = null;
    
    /** Data listing grid */
    private $grid = null;
    
    /** Data listing action set */
    private $actionSet = null;
    
    /** Data listing columns */
    private $columns = array();

    /**
     * Constructor
     */
    public function  __construct($title = null, $name = null) {
        parent::__construct();
        $this->setIdAutoGenerated(true);
        $this->setHelpIdRequired(true);
        $this->setItemGroup('listing');
        
        // Set name
        if ( $name != null ) {
            $this->setName($name);
        }

        $this->layout = $this->addItem(new Clevis_View_Layout());
        $this->layout->setName($this->getName());
        $this->layout->setDesign(Clevis_View_Layout::DESIGN_SIDEBAR);
        
        $panelCenter = $this->layout->addPanel(Clevis_View_Layout::POSITION_CENTER);
        $panelCenter->setName('center');
        
        // Add filter set
        $this->filterSet = new Clevis_View_FilterSet();
        
        // Set title
        if ( $title != null ) {
            $this->setTitle($title);
        }
        
        // Add store, grid, actions
        $this->grid = $panelCenter->addGrid();
        $this->grid->setName($this->getName());
        $this->actionSet = new Clevis_View_ActionSet();
        $this->actionSet->setName($this->getName());
        $this->actionSet->setParent($this);
        
        // Prepare fixed items
        $this->filterSet->setColumns(1);
        $this->filterSet->setFilterTarget($this->grid);
    }
    
    /**
     * Set data listing title
     * 
     * @param string $title 
     */
    public function setTitle($title) {
        $this->title = $title;
    }
    
    /**
     * Setup listing store
     * 
     * @param array $url
     * @param enum $mode (STORE_MODE_WRITE | STORE_MODE_READ)
     */
    public function setupStore($url, $mode = STORE_MODE_WRITE) {
        $this->store = $this->grid->setupStore($url, $mode);
    }

    /**
     * Setup listing store to parent data
     *
     * @param string $data Parent data attribute
     * @param enum $mode (STORE_MODE_WRITE | STORE_MODE_READ)
     */
    public function setupStoreData($data, $mode = STORE_MODE_WRITE) {
        $this->store = $this->grid->setupStoreData($data, $mode);
    }

    /**
     * Setup listing store dialog
     * 
     * @param Clevis_View_Dialog|string $dialog  Dialog for entity creation and editation (or its id)
     * @param array $defaultValues  Default values for entity creation and editation
     * @param string|null $url  Url for entity creation, editation and deletation, when is null use url from store
     * @param array|null $values  Entity values to determine which dialog to use (default mode is only one dialog so entity values can be null)
     * @param boolean $addItem  Flag if $dialog should by added by addItem to render tree
     * @return Clevis_View_Dialog
     */
    public function setupStoreDialog($dialog, $defaultValues = array(), $url = null, $values = null, $addItem = true) {
        return $this->grid->setupStoreDialog($dialog, $defaultValues, $url, $values, $addItem);
    }
    
    /**
     * Register to parent store
     * 
     * @param Clevis_View_Store $storeParent
     * @param array $storeQuery for example array('User_ID' => 'parent.ID')
     */
    public function setupStoreParent(Clevis_View_Store $storeParent, $storeQuery) {
        $this->store->registerToParentStore($storeParent, $storeQuery);
    }

    /**
     * Register to parent dialog data
     *
     * @param Clevis_View_Dialog $storeParent
     * @param array $storeQuery for example array('User_ID' => 'parent.ID')
     */
    public function setupStoreParentData(Clevis_View_Dialog $storeParent, $storeQuery) {
        $this->store->registerToParentDialog($storeParent, $storeQuery);
    }

    /**
     * Register to parent data for current item
     * 
     * @param $dataAttribute
     * @return void
     */
    public function setupStoreDataForCurrentItem($dataAttribute) {
        $this->store->registerToParentDataForCurrentItem($dataAttribute);
    }

    /**
     * Add data item to store
     * 
     * @param $dataItem
     * @return void
     */
    public function setupStoreAddDataItem($dataItem) {
        $this->store->addDataItem($dataItem);
    }

    /**
     * Add filter item to store
     *
     * @param Clevis_View_Form_Item $filterItem
     */
    public function setupStoreAddFilterItem(Clevis_View_Form_Item $filterItem) {
        $this->store->addFilterItem($filterItem);
    }
    
    /**
     * Set filter default 
     * 
     * @param string $filterDefault (FILTER_ENABLE | FILTER_DISABLE)
     */
    public function setFilterDefault($filterDefault) {
        $this->filterDefault = $filterDefault;
    }
    
    /**
     * Get data listing store
     * 
     * @return Clevis_View_Store
     */
    public function getStore() {
        return $this->store;
    }

    /**
     * Get store entity
     * 
     * @return Clevis_View_Entity
     */
    public function getStoreEntity() {
        return $this->store->getEntity();
    }
    
    /**
     * Get data listing action set
     * 
     * @return Clevis_View_ActionSet
     */
    public function getActionSet() {
        return $this->actionSet;
    }
    
    /**
     * Get data listing grid
     * 
     * @return Clevis_View_Grid
     */
    public function getGrid() {
        return $this->grid;
    }

    /**
     * @override
     */
    public function setStyleWidth($width) {
        $this->layout->setStyleWidth($width);
    }

    /**
     * @override
     */
    public function setStyleHeight($height) {
        $this->layout->setStyleHeight($height);
    }

    /**
     * @override
     */
    public function setStyleMinWidth($minWidth)
    {
        $this->layout->setStyleMinWidth($minWidth);
    }

    /**
     * @override
     */
    public function setStyleMinHeight($minHeight)
    {
        $this->layout->setStyleMinHeight($minHeight);
    }


    /**
     * Set filter column count
     * 
     * @param $columnCount
     * @return void
     */
    public function setFilterColumnCount($columnCount) {
        $this->filterSet->setColumns($columnCount);
    }
    
    /**
     * Add column to data listing
     * 
     * @param string $type
     * @param string $field
     * @param string $title
     * @param integer $width 
     * @return Clevis_View_ListingColumn
     */
    public function addColumn($type, $field, $title, $width = null) {
        $column = new Clevis_View_ListingColumn();
        $column->setType($type | $this->filterDefault);
        $column->setField($field);
        $column->setTitle($title);
        if ( $width != null )
            $column->setWidth($width);
        $this->columns[] = $column;
        return $column;
    }

    /**
     * Set column style formatter
     *
     * @param string $columnField
     * @param string $columnStyleFormat
     * @return void
     */
    public function setColumnStyle($columnField, $columnStyleFormat) {
        $this->grid->setColumnStyle($columnField, $columnStyleFormat);
    }

    /**
     * Set default sorting to column
     * 
     * @param $columnField
     * @return void
     */
    public function setColumnSort($columnField) {
        $this->grid->setColumnSort($columnField);
    }

    /**
     * Auto size first text column (or column with width "auto")
     *
     * @var bool
     */
    public function setColumnAutoSize($columnAutoSize) {
        $this->grid->setColumnAutoSize($columnAutoSize);
    }
    
    /**
     * Add action text
     * 
     * @param string $text 
     */
    public function addActionText($text) {
        $code = new Clevis_View_Code();
        $code->setCode($text);
        $this->actionSet->addItem($code);
    }
    
    /**
     * Add action for data listing
     * 
     * @param string $name
     * @param string $title
     * @param string $image
     * @param string $menuAttribute
     * @param array $menuValues 
     * @return Clevis_View_Action
     */
    public function addAction($name, $title, $image, $menuAttribute = null, $menuValues = null, $contextMenuOnly = false) {
        if ( $menuAttribute == null ) {
            $action = $this->actionSet->addAction($name, $title, $image, null, $contextMenuOnly);
        } else {
            $action = $this->actionSet->addActionMenu($name, $title, $image, $menuAttribute, $menuValues);
        }
        return $action;
    }
    
    /**
     * Add actions by fill type
     *
     * @param string $fillType action set fill type
     * @param string $name action name to add to title
     * @return void
     */
    public function addActions($fillType, $name = null) {
        $this->actionSet->fill($fillType, $name);
    }

    /**
     * Show action set labels
     * 
     * @param bool $showLabels
     * @return void
     */
    public function showActionSetLabels($showLabels = true) {
        $this->actionSet->setShowLabels($showLabels);
    }

    /**
     * Show action set
     *
     * @param bool $showActionSet
     * @return void
     */
    public function hideActionSet() {
        $this->actionSet->setStyle('display', 'none'); 
    }

    /**
     * Add listing panel grid
     *
     * @param $title
     * @param $position
     * @return Clevis_View_ListingPanelGrid
     */
    public function addPanelGrid($title, $name = null, $position = Clevis_View_Layout::POSITION_LEFT) {
        $panel = new Clevis_View_ListingPanelGrid($title, $name);
        if ( $position == Clevis_View_Layout::POSITION_LEFT || $position == Clevis_View_Layout::POSITION_RIGHT )
            $panel->setStyleWidth(100);
        else
            $panel->setStyleHeight(100);
        return $this->layout->addItem($panel, $position);
    }

    /**
     * Add listing panel
     *
     * @param $title
     * @param $position
     * @return Clevis_View_Panel
     */
    public function addPanel($position = Clevis_View_Layout::POSITION_LEFT) {
        $panel = new Clevis_View_Panel();
        if ( $position == Clevis_View_Layout::POSITION_TOP )
            $panel->setDojoAttribute('layoutPriority', 2);
        return $this->layout->addItem($panel, $position);
    }
    
    /**
     * Bind listing grid to dynamic param
     * 
     * @param string $dynamicParam
     */
    public function bindDynamicParam($dynamicParam) {
        $this->grid->bindDynamicParam($dynamicParam);
    }
    
    /**
     * Pre render listing
     *
     * @return string
     */
    public function preRender() {
        assert($this->store != null);

        // Set store to grid
        $this->grid->setStore($this->store);

        $this->actionSet->preRenderUpdateItems();
        if ( $this->actionSet->getItemCount() > 0 )
            $this->layout->addItem($this->actionSet, Clevis_View_Layout::POSITION_BOTTOM, true);
        else
            $this->actionSet = null;

        // Add filters
        foreach ( $this->columns as $column ) {
            $column->appendToGrid($this->grid);
            $column->appendToFilterSet($this->filterSet);
        }

        if ( $this->title != false ) {
            // Init panel top
            $panelTop = $this->layout->addPanel(Clevis_View_Layout::POSITION_TOP);
            $panelTop->setClass('clevis-view-listing-top');
            $panelTop->setName($this->getName() . '_top');

            $contentId = $this->getId() . '_content';
            $togglerId = $this->getId() . '_toggler';
            $countId = $this->getId() . '_counter';
            $togglerShow = Clevis_Image::render('image-pack', Clevis_Image::ICON_SMALL, 'clevis-view-listing-image') . 'Zobrazit filtrování';
            $togglerHide = Clevis_Image::render('image-unpack', Clevis_Image::ICON_SMALL, 'clevis-view-listing-image') . 'Skrýt filtrování';

            // Prepare title output
            $output = '<h2>';
            $output .= $this->title;
            $output .= ' (<span id="' . $this->getGrid()->getId() . '-counter"></span>)';

            // Add toggle
            if ( $this->filterSet->getItemCount() > 0 ) {
                $toggler =
                    'require([\'clevis/view/toggler\'], function(toggler){ toggler.toggle({' .
                        'content: \'' . $contentId . '\',' .
                        'toggler: \'' . $togglerId . '\',' .
                        'togglerShow: \'' . str_replace('"', '\\\'', $togglerShow) . '\',' .
                        'togglerHide: \'' . str_replace('"', '\\\'', $togglerHide) . '\',' .
                        'after: function(){' . $this->layout->getId(). '.layout();}' .
                    '}); });';
                $this->filterSet->addAction('hide', 'Skrýt filtr.', 'image-delete', $toggler);

                $output .= '<div id="' . $togglerId . '" class="clevis-view-listing-toggle" onClick="' . $toggler . '">' . $togglerShow . '</div>';

            }
            $output .= '</h2>';

            // Add title to panel top
            $panelTop->addCode($output);
            // Add filter set to panel top
            if ( $this->filterSet->getItemCount() > 0 ) {
                $content = $panelTop->addElementDiv();
                $content->setId($contentId);
                $content->setClass('clevis-view-listing-content');
                $content->addItem($this->filterSet);
            }
        }

        return parent::preRender();
    }

    /**
     * Render listing
     * 
     * @return string
     */
    public function render() {
        $output = '';
        $output .= '<!-- LISTING BEGIN [' . $this->getName() . '] -->' . PHP_EOL;

        $output .= '<div dojoType="clevis.view.Listing" id="' . $this->getId() . '" jsId="' . $this->getId() . '" style="display: none;"';
        $output .= ' layout="' . $this->layout->getId() . '"';
        $output .= ' grid="' . $this->grid->getId() . '"';
        $output .= ' store="' . $this->store->getId() . '"';
        if ( $this->actionSet != null ) {
            $output .= ' actionSet="' . $this->actionSet->getId() . '"';
        }
        $output .= '></div>' . PHP_EOL;

        $output .= parent::render();

        $output .= '<!-- LISTING END [' . $this->getName() . '] -->' . PHP_EOL;
        return $output;
    }
}
