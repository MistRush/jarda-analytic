<?php

/**
 * Class that represents group
 *
 * @author Pavel Cihlar
 */
class Clevis_View_Group extends Clevis_View_Container {

    /**
     * Action title
     *
     * @var string
     */
    private $title;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setClass('clevis-view-group');
        $this->setIdAutoGenerated(true);
        $this->setHelpIdRequired(true);
        $this->setItemGroup('group');
    }

    /**
     * Set action title
     *
     * @param string $title
     * @return void
     */
    public function setTitle($title) {
        $this->title = $title;
    }

    /**
     * Get action title
     *
     * @return $title
     */
    public function getTitle() {
       return $this->title;
    }

    /**
     * Render
     *
     * @return string
     */
    public function render() {
        $legendId = $this->getId() . '_legend';

        $output = $this->renderScript();
        $output .= $this->renderBeginTag('fieldset');
        if ( $this->getTitle() )
            $output .= '<legend id="' . $legendId .'" jsId="' . $legendId . '">' . $this->getTitle() . '</legend>' . PHP_EOL;
        $output .= $this->renderContent();
        $output .= $this->renderEndTag();

        return $output;
    }

}
?>