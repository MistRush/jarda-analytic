<?php

/**
 * Class that represents titlePane
 *
 * @author Pavel Cihlar
 */
class Clevis_View_PanelTitle extends Clevis_View_Container {

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setType('clevis.view.PanelTitle');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('panelTitle');
        $this->setBorder(true);
    }
    
    /**
     * Set border to panel
     * 
     * @param boolean $border Show border in panel
     */
    public function setBorder($border) {
        if ( $border ) {
            $this->setClass('clevis-view-panelTitle');
        } else {
            $this->setClass('clevis-view-panelTitle clevis-no-border');
        }
    }

    /**
     * Set title
     * 
     * @param string $title
     * @return void
     */
    public function setTitle($title) {
        $this->setDojoAttribute('title',$title);
    }

    /**
     * Get title
     *
     * @return string $title
     */
    public function getTitle() {
        return $this->getAttribute('title');
    }

    /**
     * Set href
     *
     * @param string $href
     * @return void
     */
    public function setHref($href) {
        $this->setDojoAttribute('href',$href);
    }

    /**
     * Get href
     *
     * @return string $href
     */
    public function getHref() {
        return $this->getAttribute('href');
    }

    /**
     * Pre render
     *
     * @return string
     */
    public function preRender() {
        $output = parent::preRender();

        // Render content out of panel title
        $output .= '<div id="' . $this->getId() . '_content" style="width: 100%; height: auto;">' . PHP_EOL;
        $output .= $this->renderContent();
        $output .= '</div>' . PHP_EOL;

        return $output;
    }

    /**
     * Render
     *
     * @return string
     */
    public function render() {
        $output = '';
        $output .= $this->renderBeginTag();
        $output .= $this->renderEndTag();

        return $output;
    }
    
}
?>