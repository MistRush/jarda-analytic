<?php

/**
 * Class that represents tab container
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_TabContainer extends Clevis_View_ItemElementScriptType {

    /**
     * Auto size
     * 
     * @var boolean 
     */
    private $autoSize = false;
    
    /**
     * Flag if tabcontainer is resizable
     *
     * @var boolean
     */
    private $resizeAble = false;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->addClass('clevis-view-tabContainer');
        $this->addClass(Clevis_Image::ICON_SMALL);
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('tabContainer');
        $this->setBaseType('clevis/view/TabContainer');
        $this->setDojoAttribute('doLayout', false);
        $this->setStyleFullSize();
        $this->setPosition('top');
        $this->setAutoSize(false);
    }
    
     /**
     * Set auto size
     * 
     * @param boolean $autoSize 
     */
    public function setAutoSize($autoSize) {
        $this->autoSize = $autoSize;
        if ( $autoSize ) {
            $this->setStyleWidth('auto');
            $this->setStyleHeight('auto');
        }
    }
    
    /**
     * Is auto size
     * 
     * @return boolean 
     */
    public function isAutoSize() {
        return $this->autoSize;
    }

    /**
     * Set tab position
     *
     * @param string $position
     * @return void
     */
    public function setPosition($position) {
        $this->setDojoAttribute('tabPosition',$position);
    }

    /**
     * Get tab position
     *
     * @return $position
     */
    public function getPosition() {
       return $this->getAttribute('tabPosition');
    }

    /**
     * Set resizeable
     *
     * @param boolean $resizeAble
     * @return void
     */
    public function setResizeAble($resizeAble) {
        $this->resizeAble = $resizeAble;
    }

    /**
     * Get nested
     *
     * @return boolean $resizeAble
     */
    public function isResizeAble() {
        return $this->resizeAble;
    }

    /**
     * Add subitem to this item
     *
     * @param Clevis_View_Item $item
     * @return Clevis_View_Item
     */
    public function addItem(Clevis_View_Item $item, $skipParentCheck = false) {
        parent::addItem($item);
        return $item;
    }

    /**
     * Add tab
     *
     * @param string $title
     * @return Clevis_View_Tab
     */
    public function addTab($title) {
        $tab = new Clevis_View_Tab();
        $tab->setTitle($title);
        return $this->addItem($tab);
    }

    /**
     * On init script type notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptType(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptType($scriptType);

        // Attributes
        if ( $this->isAutoSize() ) {
            $scriptType->addField('autoSize', 'true');
        }
        if ( $this->isResizeAble() ) {
            $scriptType->addField('resizeAble', 'true');
        }

        // Init tabs
        if ( $this->isAutoSize() ) {
            $initTabsBody = '';
            foreach ( $this->items as $tab ) {
                if ( empty($initTabsBody) == false )
                    $initTabsBody .= PHP_EOL;
                $initTabsBody .= 'this.addTab("' . $tab->getId() . '");';
            }
            $scriptType->addMethod('initTabs',null,$initTabsBody);
        }
    }
    
    /**
     * Pre render
     */
    public function preRender() {
        // Register to dialog for update
        $dialog = $this->getParentWithType('Clevis_View_Dialog');
        if ( $dialog != null) {
            $dialog->addUpdateItem($this);
        }

        $output = parent::preRender();

        // Render tabs out of tab container
        if ( $this->isAutoSize() ) {
            $output .= $this->renderContent();
        }

        return $output;
    }

    /**
     * Render item
     *
     * @return string
     */
    public function render() {
        $output = '';

        // Render script
        $output .= $this->renderScript();

        // Render tab container with fake tab
        $output .= $this->renderBeginTag();
        if ( $this->isAutoSize() == false ) {
            $output .= $this->renderContent();
        } else {
            $output .= '<div id="' . $this->getId() . '.tabFake" jsId="' . $this->getId() . '.tabFake" dojoType="dijit.layout.ContentPane"></div>' . PHP_EOL;
        }
        $output .= $this->renderEndTag();
        
        return $output;
    }
}
?>