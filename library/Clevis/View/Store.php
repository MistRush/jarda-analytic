<?php

/**
 * Class that represents store
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Store extends Clevis_View_ItemElementScriptType {

    /**
     * URL
     *
     * @var string
     */
    private $url;

    /**
     * URL filter
     *
     * @var string
     */
    private $urlParameters;

    /**
     * Connected items to this stores
     *
     * @var array of Clevis_View_ItemElement
     */
    private $connectedItems = array();

    /**
     * Array of parent stores
     *
     * @var array
     */
    private $storeParents = array();
    
    /**
     * Children stores
     *
     * @var array
     */
    private $storeChildren = array();

    /**
     * Data items
     * 
     * @var array
     */
    private $dataItems = array();

    /**
     * Filter items
     *
     * @var array
     */
    private $filterItems = array();

    /**
     * Entity
     *
     * @var array of Clevis_View_Entity
     */
    private $entity;

    /**
     * Merge
     *
     * @var boolean
     */
    private $dataMerge = false;

    /**
     * Merge columns
     *
     * @var array
     */
    private $dataMergeColumns;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('store');
        $this->setBaseType('clevis/view/Store');
        // Store is always data item (it will connect to first data parent)
        $this->dataSetIsItem(true);
        $this->setClass('clevis-hidden');
        $this->setAutoRefresh(true);
    }

    /**
     * Set url
     *
     * @param string $url
     */
    public function setUrl($url) {
        $this->url = $url;
    }

    /**
     * Set url filter
     *
     * @param string $urlParameters
     */
    public function setUrlParameters($urlParameters = null) {
        $this->urlParameters = $urlParameters;
    }

    /**
     * Sets whether store should load data on every request or only for the first time
     *
     * @param boolean $dynamic
     */
    public function setDynamic($dynamic) {
        $this->setDojoAttribute('dynamic', $dynamic);
    }

    /**
     * @return true if store should load data on every request, false otherwise
     */
    public function isDynamic() {
        $this->getDojoAttribute('dynamic');
    }

    /**
     * Sets whether store should be able to modify data.
     *
     * @param boolean $readOnly
     */
    public function setReadOnly($readOnly) {
        $this->setDojoAttribute('readOnly', $readOnly);
    }

    /**
     * @return true if store isn't able to modify data, false otherwise
     */
    public function isReadyOnly() {
        return $this->getDojoAttribute('readOnly');
    }

    /**
     * Specifies whether store should be auto-refreshed on page load
     *
     * @param $autoRefresh
     */
    public function setAutoRefresh($autoRefresh)
    {
        $this->setDojoAttribute('autoRefresh', $autoRefresh);
    }

    /**
     * @return true if store should be auto-refreshed on page load,
     *         false otherwise
     */
    public function isAutoRefresh()
    {
        return $this->getDojoAttribute('autoRefresh');
    }

    /**
     * Has url
     *
     * @return boolean
     */
    public function hasUrl() {
        return $this->url != null;
    }

    /**
     * Get url
     *
     * @return string $url
     */
    public function getUrl() {
        return $this->url;
    }

    /**
     * Register to parent store
     * 
     * @param Clevis_View_Store $storeParent
     * @param array $storeQuery for example array('User_ID' => 'parent.ID')
     */
    public function registerToParentStore(Clevis_View_Store $storeParent, $storeQuery) {
        $this->storeParents[] = array('store' => $storeParent, 'query' => $storeQuery);
        $storeParent->storeChildren[] = $this;
    }

    /**
     * Register to parent dialog
     *
     * @param Clevis_View_Dialog $storeParent
     * @param array $storeQuery for example array('User_ID' => 'parent.ID')
     */
    public function registerToParentDialog(Clevis_View_Dialog $storeParent, $storeQuery) {
        $this->storeParents[] = array('dialog' => $storeParent, 'query' => $storeQuery);
        $storeParent->dataAddItem($this);
    }

    /**
     * Add item that is connected to store and need to be refreshed
     */
    public function addConnectedItem(Clevis_View_ItemElement & $connectedItem) {
        $this->connectedItems[] = $connectedItem;
    }

    /**
     * Remove connected item
     */
    public function removeConnectedItem(Clevis_View_ItemElement & $connectedItem) {
        array_remove_reference($this->connectedItems,$connectedItem);
    }

    /**
     * Add data item to store
     * 
     * @param array $dataItem
     */
    public function addDataItem($dataItem) {
        $this->dataItems[] = $dataItem;
    }

    /**
     * Add filter item to store
     *
     * @param Clevis_View_Form_Item $filterItem
     */
    public function addFilterItem(Clevis_View_Form_Item $filterItem) {
        $this->filterItems[] = $filterItem;
    }

    /**
     * Register this store to parent dialog data if some exists
     *
     * @param string $dataAttribute
     */
    public function registerToParentData($dataAttribute) {
        if ( $this->hasName() == false )
            $this->setName(str_replace('.','_', $dataAttribute));
        $this->setDojoAttribute('dataAttribute', $dataAttribute);
    }

    /**
     * Has data attribute
     *
     * @return boolean
     */
    public function isRegisteredToParentData() {
        return $this->hasDojoAttribute('dataAttribute');
    }

    /**
     * Get data attribute
     *
     * @return $dataAttribute
     */
    public function getRegisteredToParentDataAttribute() {
        return $this->getDojoAttribute('dataAttribute');
    }

    /**
     * Register this store to parent dialog data if some exists
     *
     * @param string $dataAttribute
     */
    public function registerToParentDataForCurrentItem($dataAttributeForCurrentItem) {
        $this->setDojoAttribute('dataAttributeForCurrentItem', $dataAttributeForCurrentItem);
    }

    /**
     * Get entity
     *
     * @return Clevis_View_Entity
     */
    public function getEntity() {
        if ( $this->entity == null ) {
            $this->entity = new Clevis_View_Entity();
        }
        return $this->entity;
    }

    /**
     * Set merge url
     *
     * @param string $url
     */
    public function setDataMergeUrl($url, $mergeColumns = null) {        
        $this->setUrl($url);
        $this->dataMerge = true;
        $this->dataMergeColumns = $mergeColumns;
    }

    /**
     * On init script notification
     *
     * @param Clevis_View_Script $script
     */
    protected function onInitScript(Clevis_View_Script & $script) {
        parent::onInitScript($script);

        $view = $this->getView();

        if ( !$this->isReadyOnly()) {
            $script->addOnLoad(
                'var view = registry.byId("' . $view->getId() . '");' . PHP_EOL .
                'view.addObjectMethodToReset("' . $this->getId() . '", "clear");' . PHP_EOL .
                $this->getId() . '.setEntity(' . $this->entity->getId() . ');',
                array('dijit/registry' => 'registry')
            );
        }
    }

    /**
     * On init script type notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptType(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptType($scriptType);
        $scriptType->addMethod('initConnectedWidgets');
        $scriptType->addMethod('initFilterWidgets');
        $scriptType->addMethod('initChildStores');
        $scriptType->addMethod('initData',array('data'));
        if ( !$this->isReadyOnly() ) {
            $scriptType->addMethod('createEntity',array('parameters'));
            $scriptType->addMethod('editEntity',array('entity'));
            $scriptType->addMethod('deleteEntity',array('entity'));
            $scriptType->addMethod('saveEntity');
        }
    }

    /**
     * On init script type methods notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptTypeMethods(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptTypeMethods($scriptType);

        $initConnectedItemsBody = '';
        foreach ( $this->connectedItems as $connectedItem ) {
            if ( empty($initConnectedItemsBody) == false ) {
                $initConnectedItemsBody .= PHP_EOL;
            }
            $initConnectedItemsBody .= 'this.connectedWidgets.push("' . $connectedItem->getId() . '");';
        }
        $scriptType->provideMethodBody('initConnectedWidgets', $initConnectedItemsBody);

        $initFilterWidgetsBody = '';
        foreach ( $this->filterItems as $filterItem ) {
            if ( empty($initFilterWidgetsBody) == false ) {
                $initFilterWidgetsBody .= PHP_EOL;
            }
            $initFilterWidgetsBody .= 'this.filterWidgets.push("' . $filterItem->getId() . '");';
        }
        $scriptType->provideMethodBody('initFilterWidgets', $initFilterWidgetsBody);

        $initParentStoresBody = '';
        foreach ( $this->storeParents as $parentStore ) {
            if ( empty($initParentStoresBody) == false ) {
                $initParentStoresBody .= PHP_EOL;
            }
            if ( array_key_exists('store', $parentStore) ) {
                $initParentStoresBody .= 'this.parents.push({getParent: function(){ return ' . $parentStore['store']->getId() . ';}, ';
                $initParentStoresBody .= 'getValue: function(field){ var store = this.getParent(); if ( store.currentItemIndex == null ) { return null; } return store.getCurrentItemValue(field); }, ';
                $initParentStoresBody .= 'query: ' . json_encode($parentStore['query']) . '});' . PHP_EOL;
            }
            else if ( array_key_exists('dialog', $parentStore) ) {
                $initParentStoresBody .= 'this.parents.push({getParent: function(){ return ' . $parentStore['dialog']->getId() . ';}, ';
                $initParentStoresBody .= 'getValue: function(field){ var dialog = this.getParent(); return dialog.data[field]; }, ';
                $initParentStoresBody .= 'query: ' . json_encode($parentStore['query']) . '});' . PHP_EOL;
            }
        }
        $scriptType->provideMethodBody('initParents', $initParentStoresBody);
        
        $initChildStoresBody = '';
        foreach ( $this->storeChildren as $childStore ) {
            if ( empty($initChildStoresBody) == false ) {
                $initChildStoresBody .= PHP_EOL;
            }
            $initChildStoresBody .= 'this.childStores.push({getStore: function(){ return ' . $childStore->getId() . ';}});' . PHP_EOL;
        }
        $scriptType->provideMethodBody('initChildStores', $initChildStoresBody);

        $initDataBody = 'this.inherited(arguments);';
        foreach ( $this->dataItems as $dataItem ) {
            if ( empty($initDataBody) == false ) {
                $initDataBody .= PHP_EOL;
            }
            $initDataBody .= 'this.addDataItem(data,' . json_encode($dataItem) . ');';
        }
        $scriptType->provideMethodBody('initData',$initDataBody);

        if ( !$this->isReadyOnly() ) {
            $scriptType->provideMethodBody('createEntity',
                'return this.entity.createEntity(parameters);'
            );
            $scriptType->provideMethodBody('editEntity',
                'if ( entity == null )' . PHP_EOL .
                    '   return false;' . PHP_EOL .
                    'this.currentEntity = entity;' . PHP_EOL .
                    'return this.entity.editEntity(entity);'
            );
            $scriptType->provideMethodBody('deleteEntity',
                'if ( entity == null )' . PHP_EOL .
                    '   return false;' . PHP_EOL .
                    'this.currentEntity = entity;' . PHP_EOL .
                    'return this.entity.deleteEntity(entity);'
            );
        }
    }

    /**
     * Pre render store
     *
     * @return string
     */
    public function preRender() {       
        if ( $this->url != null ) {
            // Probably some parent dialog is added to page twice, check it!
            assert(is_array($this->url));

            // Prepare url filter
            if ( $this->urlParameters == null ) {
                $this->urlParameters = array();
            }
            foreach ( $this->url as $key => $value ) {
                if ( $key == 'action' || $key == 'module' || $key == 'controller' ) {
                    continue;
                }
                $this->urlParameters[$key] = $value;                
            }
            // Prepare url
            $this->url = Clevis_Helper::formatUrl($this->url, false);
            // Set url and url filter as attribute
            $this->setDojoAttribute('url', $this->url);
            $this->setDojoAttribute('urlParameters', $this->urlParameters);
        }
        if ( !$this->isReadyOnly() ) {
            $entity = $this->getEntity();
            if ( !$entity->hasControllerUrl() ) {
                $entity->setControllerUrl($this->url, false);
            }
            $this->addItem($entity);
        }
        if ( $this->dataMerge ) {
            $this->setDojoAttribute('dataMerge', true);
        }
        if ( $this->dataMergeColumns != null ) {
            $this->setDojoAttribute('dataMergeColumns', $this->dataMergeColumns);
        }
        $output = parent::preRender();
        return $output;
    }

}
