<?php

/**
 * Class that represents tab
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Tab extends Clevis_View_Container {

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setClass('clevis-view-tab');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('tab');
        $this->setStyle('display', 'table');
        $this->enableScript(true);
        $this->enableScriptType(true);
        $this->setBaseType('clevis/view/Tab');
        
        // Make data connection layer
        $this->dataSetIsItem(true);
        $this->dataSetIsParent(true);
    }

    /**
     * Set title
     * 
     * @param string $title
     * @return void
     */
    public function setTitle($title) {
        $this->setDojoAttribute('title',$title);
    }

    /**
     * Get title
     *
     * @return string $title
     */
    public function getTitle() {
        return $this->getAttribute('title');
    }

    /**
     * Set tab image
     *
     * @param string $image
     * @return void
     */
    public function setImage($image) {
        $this->setDojoAttribute('iconClass', $image);
    }

    /**
     * Get tab Icon Class
     *
     * @return $iconClass
     */
    public function getIconClass() {
       return $this->getAttribute('iconClass');
    }

    /**
     * @Override
     */
    protected function onInitScriptType(Clevis_View_ScriptType & $scriptType)
    {
        parent::onInitScriptType($scriptType);

        // Get parent tab if exists
        $parentTabContainer = $this->getParent();
        $scriptType->addField('dataValidateSelector', 'function(){ return ' . $this->getId() . '.getTabContainer().selectChild(' . $this->getId() . '); }');
    }

}
?>