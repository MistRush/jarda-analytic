    /**
     * Set minimum width
     *
     * @param $minWidth
     * @return void
     */
    public function setStyleMinWidth($minWidth) {
        if ( is_numeric($minWidth) )
            $minWidth = $minWidth . 'px';
        $this->setStyle('min-width',$minWidth);
