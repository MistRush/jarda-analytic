        $this->setStyleWidth('auto');
        $this->setStyleHeight('auto');
    }

    /**
     * Set style hidden
     */
    public function setStyleHidden() {
        $this->setStyle('display','none');
    }

    /**
     * Get style value
     *
     * @return value
     */
    public function getStyle($style) {
        if ( array_key_exists($style,$this->style) ) {
            return $this->style[$style];
        }
        else
            return null;
    }

    /**
     * Unset attribute value
     *
     * @return void
     */
    public function unsetAttribute($attribute) {
        array_remove_by_key($this->attributes,$attribute);
    }
    
    /**
     * Pre render
     *
     * @return string
     */
    public function preRender() {
        $output = parent::preRender();
        
        if ( $this->dataIsItem() ) {
            $dataParent = $this->dataGetParent();
            if ( $dataParent != null ) {
                $this->setDojoAttribute('dataParent', $dataParent->getId());
            }
        }
        
        return $output;
    }

    /**
     * Render item's attributes
     * 
     * @return string
     */
    protected function renderAttributes() {
        $output = '';
        foreach ( $this->attributes as $attribute => $value ) {
            if ( is_array($value) )
                continue;
            if ( is_bool($value) ) {
                if ( $value == true )
                    $value = 'true';
                else if ( $value == false )
                    $value = 'false';
            }
            $value = str_replace('"','\'',$value);
            $output .= ' ' . $attribute . '="' . $value . '"';
        }

        $outputStyle = '';
        foreach ( $this->style as $style => $value ) {
            if ( $outputStyle != '' )
                $outputStyle .= ' ';
            $outputStyle .= $style . ': ' . $value . ';';
        }
        if ( $outputStyle != '' ) {
            $output .= ' style="' . $outputStyle . '"';
        }

        $outputProps = '';
        foreach ( $this->dojoAttributes as $attribute => $value ) {
            if ( $value === null) {
                $value = 'null';
            } else if ( is_bool($value) ) {
                if ( $value == true )
                    $value = 'true';
                else if ( $value == false )
                    $value = 'false';
            } else if ( is_array($value) ) {
                $value = Zend_Json::encode($value);
            } else {
                if ( startsWith($value, '\\') ) {
                    $value = substr($value, 1);
                }
                else if ( !startsWith($value, 'function') ) {
                    $value = str_replace('"','\\\'',$value);
                    $value = '\'' . $value . '\'';
                }
            }
            $value = str_replace('"','\'',$value);
            if ( strlen($outputProps) > 0 ) {
                $outputProps .= ', ';
            }
            $outputProps .= $attribute . ': ' . $value;
        }
        if ( $this->helpIdRequired ) {
            if ( strlen($outputProps) > 0 ) {
                $outputProps .= ', ';
            }
            $outputProps .= 'helpId: \'' . $this->getHelpId() . '\'';
        }
        if ( $outputProps != '' ) {
            $output .= ' data-dojo-props="' . $outputProps . '"';
        }

        return $output;
    }

    /**
     * Render item's begin
     *
     * @return string
     */
    protected function renderBeginTag($tag = null,$attributes = array()) {
        if ( $tag == null )
            $tag = $this->tag;
        else
            $this->tag = $tag;
        if ( $tag == null )
            throw new Exception('Tag is null, so can\'t render begin tag.');
        $output = '<' . $tag;
        if ( $this->hasId() )
            $output .= ' id="' . $this->getId() . '" jsId="' . $this->getId() . '"';
        if ( $this->hasName() )
             $output .= ' name="' . $this->getName() . '"';
        $class = $this->getClass();
        if ( strlen($class) > 0 )
             $output .= ' class="' . $class . '"';
        if ( $this->hasType() )
             $output .= ' data-dojo-type="' . $this->getType() . '"';
        $output .= $this->renderAttributes();
        foreach ( $attributes as $attribute => $value ) {
            $output .= ' ' . $attribute . '="' . $value . '"';
        }
        if ( $this->tagSingle ) {
            $output .= '/>' . PHP_EOL;
        } else {
            $output .= '>' . PHP_EOL;
            foreach ( $this->attributes as $attribute => $value ) {
                if ( is_array($value) )
                    $output .= '<' . $attribute . '>' . $value['value'] . '</' . $attribute . '>' . PHP_EOL;
            }
        }
        return $output;
    }

    /**
     * Render item's end
     *
     * @return string
     */
    protected function renderEndTag() {
        if ( $this->tag == null )
            throw new Exception('Tag is null, so can\'t render end tag.');
        $output = '</' . $this->tag . '>' . PHP_EOL;
        $this->beginTag = null;
        return $output;
    }

    /**
     * Render item without tag
     *
     * @return string
     */
    public function renderContent() {
        return parent::render();
    }

    /**
     * Render item
     * 
     * @return string
     */
    public function render() {
        $output = '';
        if ( $this->hasTag() )
            $output .= $this->renderBeginTag();
        if ( $this->tagSingle == false ) {
            $content = $this->renderContent();
            if ( $content == '' )
                $output = trim($output,PHP_EOL);
            $output .= $content;
            if ( $this->hasTag() )
                $output .= $this->renderEndTag();
        }
        return $output;
    }

