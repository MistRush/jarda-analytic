<?php

/**
 * Class that represents every item that is element in view
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_ItemElement extends Clevis_View_Item {

    /**
     * Tag of item
     *
     * @var string
     */
    private $tag;

    /**
     * Single tag? (ie <input />)
     *
     * @var bool
     */
    private $tagSingle = false;

    /**
     * Identifier of item
     *
     * @var string
     */
    private $id;

    /**
     * Help identifier for item
     *
     * @var string
     */
    private $helpId;

    /**
     * @var true if help identifier should be assigned
     */
    private $helpIdRequired = false;

    /**
     * Group of item
     *
     * @var string
     */
    private $itemGroup;

    /**
     * @var true if item should not ask it's parent for his identifiers to be prepended to the item's identifiers
     */
    private $itemRoot = false;

    /**
     * Name of item
     *
     * @var string
     */
    private $name;

    /**
     * Manage that if id is null then it is auto generated
     *
     * @var boolean
     */
    private $idAutoGenerated = false;

    /**
     * CSS Class of item
     *
     * @var string
     */
    private $class = array();

    /**
     * Type of item
     *
     * @var string
     */
    private $type;

    /**
     * Manage if type is set by id
     *
     * @var boolean
     */
    private $typeById = false;

    /**
     * Attributes of item
     * 
     * @var array of strings
     */
    private $attributes = array();

    /**
     * Dojo attributes of item
     *
     * @var array of string
     */
    private $dojoAttributes = array();

    /**
     * Style attribute
     * 
     * @var array
     */
    private $style = array();

    /**
     * Id auto generate counter
     * 
     * @var array of count
     */
    private static $idAutoGenerateCount = array();

    /**
     * Type auto generate counter
     *
     * @var array of count
     */
    private static $typeAutoGenerateCount = array();

    /**
     * Constructor
     */
    public function __construct() {
        parent::__construct();
    }
    
    /**
     * Set item tag
     *
     * @param string $tag
     * @return void
     */
    public function setTag($tag, $tagSingle = false) {
        $this->tag = $tag;
        $this->tagSingle = $tagSingle;
    }

    /**
     * Has item tag
     *
     * @return boolean
     */
    public function hasTag() {
        return $this->tag != null;
    }

    /**
     * Get item tag
     * 
     * @return $tag
     */
    public function getTag() {
        return $this->tag;
    }

    /**
     * Set item identifier
     *
     * @param string $id
     * @return void
     */
    public function setId($id) {
        $oldId = $this->id;
        $this->id = $id;
        $this->onSetId($oldId,$this->id);
    }

    /**
     * Has item identifier
     *
     * @return boolean
     */
    public function hasId() {
        $this->checkIdAutoGenerated();
        return $this->id != null;
    }

    /**
     * Get item identifier
     *
     * @return $id
     */
    public function getId() {
        $this->checkIdAutoGenerated();
        return $this->id;
    }

    /**
     * Set item group for identifiers
     *
     * @param $itemGroup
     */
    public function setItemGroup($itemGroup) {
        $this->itemGroup = $itemGroup;
    }

    /**
     * Set if item is root for identifiers
     *
     * @param $itemRoot
     */
    public function setItemRoot($itemRoot) {
        $this->itemRoot = $itemRoot;
    }

    /**
     * Set help identifier for the item
     *
     * @param $helpIdRequired
     * @param $helpIdRoot
     */
    public function setHelpIdRequired($helpIdRequired) {
        $this->helpIdRequired = $helpIdRequired;
    }

    /**
     * Get help id
     */
    public function getHelpId() {
        if ($this->helpId === null) {
            $parentHelpId = '';
            if (!$this->itemRoot && $this->parent != null) {
                $parentHelpId = $this->parent->getHelpId();
            }
            $helpId = '';
            if ($this->helpIdRequired) {
                if ( $this->itemGroup != null ) {
                    if ( $this->name != null ) {
                        $helpId .= $this->itemGroup . '(' . $this->name . ')';
                    } else {
                        $helpId .= $this->itemGroup;
                    }
                } else if ( $this->name != null ) {
                    $helpId .= $this->name;
                } else {
                    throw new Exception('Cannot get help id for ' . $this->get_class() . ' with id "' .
                        $this->getId() . '" because it doesn\'t have set name or group!');
                }
                if ( $parentHelpId != null ) {
                    $this->helpId = $parentHelpId . '.' . $helpId;
                } else {
                    $this->helpId = $helpId;
                }
            } else {
                $this->helpId = $parentHelpId;
            }
        }
        if ( $this->helpId === false ) {
            return null;
        }
        return $this->helpId;
    }

    /**
     * Set item name
     *
     * @param string $name
     * @return void
     */
    public function setName($name) {
        $this->name = $name;
    }

    /**
     * Has item name
     *
     * @return boolean
     */
    public function hasName() {
        return $this->name != null;
    }

    /**
     * Get item name
     *
     * @return $name
     */
    public function getName() {
        return $this->name;
    }

    /**
     * Set if id is null then it is auto generated
     *
     * @param boolean $idAutoGenerated
     * @param boolean $idAutoGeneratedName base name for auto generate
     * @return void
     */
    public function setIdAutoGenerated($idAutoGenerated) {
        $this->idAutoGenerated = $idAutoGenerated;
    }

    /**
     * Checks if id is auto generated when is null
     *
     * @return boolean
     */
    public function isIdAutoGenerated() {
        return $this->idAutoGenerated;
    }

    /**
     * Check if id is auto generated and if it is and is null, do auto generate
     */
    public function checkIdAutoGenerated() {
        if ( $this->idAutoGenerated && $this->id == null ) {
            // Get group name
            $name = $this->itemGroup;
            if ( $name != null ) {
                if ( $this->name != null ) {
                    $name = $name . '_' . $this->name;
                }
            }
            else if ( $this->parent != null ) {
                $name = $this->parent->getId();
                if ( $name != null ) {
                    if ( $this->name != null ) {
                        $name = $name . '_' . $this->name;
                    }
                    else {
                        $name = $name . '_' . 'element';
                    }
                } else {
                    if ( $this->name != null ) {
                        $name = $this->name;
                    }
                    else {
                        $name = 'element';
                    }
                }
            }

            // Check if id exists
            if ( array_key_exists($name,Clevis_View_ItemElement::$idAutoGenerateCount) == false ) {
                Clevis_View_ItemElement::$idAutoGenerateCount[$name] = 0;
                $this->setId($name);
            }
            else {
                // Get count for base name and incerement
                $count = ++Clevis_View_ItemElement::$idAutoGenerateCount[$name];
                // Auto generate id
                $this->setId($name . $count);
            }
        }
    }

    /**
     * Set one class only
     * 
     * @param $class
     * @return void
     */
    public function setClass($class) {
        $this->class = array($class => true);
    }

    /**
     * Add item class
     *
     * @param string $class
     * @return void
     */
    public function addClass($class) {
        $this->class[$class] = true;
    }

    /**
     * Remove item class
     *
     * @param string $class
     * @return void
     */
    public function removeClass($class) {
        $this->class[$class] = false;
    }

    /**
     * Remove all item classes
     * 
     * @return void
     */
    public function removeClassAll() {
        $this->class = array();
    }

    /**
     * Check if item has class
     * 
     * @param $class
     * @return bool
     */
    public function hasClass($class) {
        return array_key_exists($class, $this->class) && $this->class[$class] == true;
    }

    /**
     * Get item class
     *
     * @return $class
     */
    public function getClass() {
        $class = '';
        foreach ( $this->class as $key => $value ) {
            if ( strlen($class) > 0 )
                $class .= ' ';
            if ( $value == true )
                $class .= $key;
        }
        return $class;
    }

    /**
     * Set item type
     *
     * @param string $type
     * @return void
     */
    public function setType($type) {
        $this->type = $type;
    }

    /**
     * Has item type
     *
     * @return boolean
     */
    public function hasType() {
        return $this->type != null;
    }

    /**
     * Get item type
     *
     * @return $type
     */
    public function getType() {
        $this->checkTypeAutoGenerated();
        return $this->type;
    }

    /**
     * If type is null, do auto generate
     */
    public function checkTypeAutoGenerated() {
        if ( $this->type == null ) {
            if ( $this->hasId() ) {
                $this->setType('type_' . $this->getId());
            } else {
                $type = 'type';
                // Get group name
                $group = $this->idAutoGeneratedGroup;
                if ( $group != null )
                    $type .= '_' . $group;
                else
                    $type .= '_element';
                // Check if id exists
                if ( array_key_exists($type,Clevis_View_ItemElement::$typeAutoGenerateCount) == false ) {
                    Clevis_View_ItemElement::$typeAutoGenerateCount[$type] = 0;
                }
                // Get count for base name and incerement
                $count = Clevis_View_ItemElement::$typeAutoGenerateCount[$type]++;
                // Set type
                $this->setType($type . $count);
            }
        }
    }

    /**
     * Set element attribute value
     *
     * @param string $attribute
     * @param string $value
     */
    public function setElementAttribute($attribute, $value, $asSubElement = false) {
        if ( $asSubElement )
            $this->attributes[$attribute] = array('value' => $value);
        else
            $this->attributes[$attribute] = $value;
    }

    /**
     * Has attribute value
     *
     * @return boolean
     */
    public function hasElementAttribute($attribute) {
        return array_key_exists($attribute, $this->attributes);
    }

    /**
     * Get attribute value
     *
     * @return value
     */
    public function getElementAttribute($attribute) {
        if ( array_key_exists($attribute, $this->attributes) ) {
            if ( is_array($this->attributes[$attribute]) ) {
                return $this->attributes[$attribute]['value'];
            } else {
                return $this->attributes[$attribute];
            }
        }
        else {
            return null;
        }
    }

    /**
     * Set dojo attribute value
     *
     * @param string $attribute
     * @param string $value
     */
    public function setDojoAttribute($attribute, $value, $asSubElement = false) {
        if ( $asSubElement ) {
            throw new Exception("TODO: Dojo migration to 1.8: Correct this!!!");
        }

        $this->dojoAttributes[$attribute] = $value;
    }

    /**
     * Has dojo attribute value
     *
     * @return boolean
     */
    public function hasDojoAttribute($attribute) {
        return array_key_exists($attribute, $this->dojoAttributes);
    }

    /**
     * Get dojo attribute value
     *
     * @return value
     */
    public function getDojoAttribute($attribute) {
        if ( array_key_exists($attribute, $this->dojoAttributes) ) {
            return $this->dojoAttributes[$attribute];
        }
        else {
            return null;
        }
    }

    /**
     * Set style value
     *
     * @param string $style
     * @param string $value
     */
    public function setStyle($style,$value) {
        $this->style[$style] = $value;
    }

    /**
     * Has style value
     *
     * @return boolean
     */
    public function hasStyle($style) {
        return array_key_exists($style,$this->style);
    }

    /**
     * Set width and height
     *
     * @param $width
     * @return void
     */
    public function setStyleSize($width,$height) {
        $this->setStyleWidth($width);
        $this->setStyleHeight($height);
    }

    /**
     * Set width
     *
     * @param $width
     * @return void
     */
    public function setStyleWidth($width) {
        if ( is_numeric($width) )
            $width = $width . 'px';
        $this->setStyle('width',$width);
    }

    /**
     * Get width
     *
     * @return string $width
     */
    public function getStyleWidth() {
        return $this->getStyle('width');
    }

    /**
     * Set height of grid
     *
     * @param $height
     * @return void
     */
    public function setStyleHeight($height) {
        if ( is_numeric($height) )
            $height = $height . 'px';
        $this->setStyle('height',$height);
    }

    /**
     * Get height
     *
     * @return string $height
     */
    public function getStyleHeight() {
        return $this->getStyle('height');
    }

    /**
     * Set minimum width and height
     *
     * @param $minWidth
     * @param $minHidth
