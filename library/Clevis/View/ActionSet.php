<?php

/**
 * Class that represents action set
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_ActionSet extends Clevis_View_ItemElementScriptType {

    /** Fill type */
    const FILL_DIALOG = 'dialog';
    const FILL_MANAGE = 'manage';
    
    /**
     * Manages if action body is auto generate
     * 
     * @var boolean
     */
    private $actionBodyAutoGenerate = true;

    /**
     * Store for action body auto generate
     *
     * @var Clevis_View_ItemHasStore
     */
    private $actionBodyAutoGenerateStore;

    /**
     * Dialog for action body auto generate
     *
     * @var Clevis_View_Dialog
     */
    private $actionBodyAutoGenerateDialog;

    /**
     * ActionSet action showLabels
     *
     * @var boolean
     */
    private $showLabels = true;
    
    /**
     * Action invoked after enter key pressed
     * 
     * @var string
     */
    private $onEnterKeyAction;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('actionSet');
        $this->setBaseType('clevis/view/ActionSet');
        $this->setTag('fieldset');
        $this->addClass('clevis-view-actionSet');
        $this->addClass(Clevis_Image::ICON_SMALL);
    }

    /**
     * Set action align
     *
     * @param string $align
     * @return void
     */
    public function setAlign($align) {
        $this->setStyle('text-align',$align);
    }

    /**
     * Get action align
     *
     * @return $align
     */
    public function getAlign() {
       return $this->getStyle('text-align');
    }

    /**
     * Set action body auto generate
     *
     * @param boolean $actionBodyAutoGenerate
     * @return void
     */
    public function setActionBodyAutoGenerate($actionBodyAutoGenerate) {
        $this->actionBodyAutoGenerate = $actionBodyAutoGenerate;
    }

    /**
     * Is action body auto generate
     *
     * @return boolean $actionBodyAutoGenerate
     */
    public function isActionBodyAutoGenerate() {
       return $this->actionBodyAutoGenerate;
    }

    /**
     * Set item has store for action body auto generate
     *
     * @param Clevis_View_ItemHasStore $actionBodyAutoGenerateGrid
     * @return void
     */
    public function setActionBodyAutoGenerateStore(Clevis_View_ItemHasStore & $actionBodyAutoGenerateStore) {
        $this->actionBodyAutoGenerateStore = $actionBodyAutoGenerateStore;
    }
    
    /**
     * Get store for action body auto generate
     *
     * @return Clevis_View_ItemHasStore
     */
    public function & getActionBodyAutoGenerateStore() {
        if ( $this->actionBodyAutoGenerateStore == null ) {
            if ( $this->hasParent() ) {
                $previousItem = $this->getParent()->getPreviousItem($this);
                while ( $previousItem != null ) {
                    // If previous item is item has store we have found it
                    if ( $previousItem instanceof Clevis_View_ItemHasStore ) {
                        $this->setActionBodyAutoGenerateStore($previousItem);
                    }
                    // Else if previous item is panel we can look inside
                    else if ( $previousItem instanceof Clevis_View_Panel ) {
                        $lastItem = $previousItem->getLastItem();
                        // If last item of panel is grid we have found it
                        if ( $lastItem instanceof Clevis_View_ItemHasStore ) {
                           $this->setActionBodyAutoGenerateStore($lastItem);
                        }
                    }
                    $previousItem = $this->getParent()->getPreviousItem($previousItem);
                }
            }
        }
        return $this->actionBodyAutoGenerateStore;
    }

    /**
     * Set dialog for action body auto generate
     *
     * @param Clevis_View_Dialog $actionBodyAutoGenerateDialog
     * @return void
     */
    public function setActionBodyAutoGenerateDialog(Clevis_View_Dialog & $actionBodyAutoGenerateDialog) {
        $this->actionBodyAutoGenerateDialog = $actionBodyAutoGenerateDialog;
    }

    /**
     * Get dialog for action body auto generate
     *
     * @return Clevis_View_Dialog $actionBodyAutoGenerateDialog
     */
    public function & getActionBodyAutoGenerateDialog() {
        if ( $this->actionBodyAutoGenerateDialog == null ) {
            $dialog = $this->getParentWithType('Clevis_View_Dialog');
            if ( $dialog != null ) {
                $this->setActionBodyAutoGenerateDialog($dialog);
            }
        }
        return $this->actionBodyAutoGenerateDialog;
    }

    /**
     * Set action attribute showLabel
     *
     * @param boolean $showLabels
     * @return void
     */
    public function setShowLabels($showLabels) {
        $this->showLabels = $showLabels;
    }

    /**
     * Get action showLabels
     *
     * @return attribute showLabels
     */
    public function isShowLabels() {
        return $this->showLabels;
    }
    
    /**
     * Set action that will be invoked after enter key is pressed
     * 
     * @param string $action 
     */
    public function setOnEnterKeyAction($action) {
        $this->onEnterKeyAction = $action;
    }

    /**
     * Add action
     *
     * @param string $name
     * @param string $title
     * @param string $image
     * @return Clevis_View_Action
     */
    public function addAction($name, $title, $image = null, $body = null, $contextMenuOnly = false) {
        // Create action
        $action = new Clevis_View_Action();
        $action->setName($name);
        $action->setTitle($title);
        if ( $image != null )
            $action->setImage($image);
        if ( $body != null )
            $action->setBody($body);
        $action->setContextMenuOnly($contextMenuOnly);
        return $this->addItem($action);
    }

    /**
     * @param $name
     * @return action with given $name
     */
    public function getAction($name)
    {
        foreach ( $this->items as $item ) {
            if ( $item->getName() == $name ) {
                return $item;
            }
        }
        return null;
    }

    /**
     * Delete action
     *
     * @param $name
     */
    public function deleteAction($name)
    {
        foreach ( $this->items as $item ) {
            if ( $item->getName() == $name ) {
                $this->deleteItem($item);
                break;
            }
        }
    }
    
    /**
     * Add action menu
     *
     * @param string $name
     * @param string $title
     * @param string $image
     * @return Clevis_View_ActionMenu
     */
    public function addActionMenu($name, $title, $image = null, $attributeValue = null, $menuValues = null) {
        // Create action
        $action = new Clevis_View_ActionMenu();
        $action->setName($name);
        $action->setTitle($title);
        if ( $image != null )
            $action->setImage($image);
        $action->setAttributeValue($attributeValue);
        if ( $menuValues != null ) {
            foreach ( $menuValues as $value => $item) {
                if (is_array($item) ) {
                    $action->addMenuItem($item['name'], array_key_exists('image', $item) ? $item['image'] : null, $value);
                } else {
                    $action->addMenuItem($item, null, $value);
                }
                    
            }
        }
        return $this->addItem($action);
    }

    /**
     * Checks if action set has action with given name
     *
     * @param string $name
     */
    public function hasAction($name) {
        foreach ( $this->items as $item ) {
            if ( $item instanceof Clevis_View_Action ) {
                if ( $item->getName() == $name )
                    return true;
            }
        }
        return false;
    }

    /**
     * Fill action set for dialog
     *
     * @param $fillType automatic fill type
     * @param $name action name to add to title
     * @return void
     */
    public function fill($type, $name = null) {
        if ( $type == Clevis_View_ActionSet::FILL_DIALOG ) {
            // Fill dialog action set
            $this->setAlign(ALIGN_RIGHT);
            $this->addAction('ok','Potvrdit','image-ok');
            $this->addAction('cancel','Zpět','image-cancel');
            $this->setOnEnterKeyAction('ok');


        } else if ( $type == Clevis_View_ActionSet::FILL_MANAGE ) {
            // Prepare name
            $addName = '';
            if ( $name != null ) {
                $addName = ' ' . $name;
            }

            // Fill grid action set
            $this->addAction('add','Vytvořit' . $addName,'image-add');
            $this->addAction('edit','Upravit' . $addName,'image-edit');
            $this->addAction('delete','Vymazat' . $addName,'image-delete');
        }
    }

    /**
     * On init script type methods notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptTypeMethods(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptTypeMethods($scriptType);
        
        // On enter key action
        if ( $this->onEnterKeyAction != null ) {
            $scriptType->setFieldValue('onEnterKey', '"onButton' . ucfirst($this->onEnterKeyAction) . '"');
        }

        // Register to dialog for update
        $dialog = $this->getParentWithType('Clevis_View_Dialog');
        if ( $dialog != null) {
            $scriptType->setFieldValue('isInDialog', 'true');
        }
        
        // Actions
        foreach ( $this->items as &$action ) {
            if ( ($action instanceof Clevis_View_Action || $action instanceof Clevis_View_ActionMenu) == false )
                continue;
            $name = 'onButton' . ucfirst($action->getName());
            $params = null;
            $body = '';
            $actionName = $action->getName();
            
            // Action menu has param data
            if ( $action instanceof Clevis_View_ActionMenu ) {
                $params = array('data');
            }
            
            if ( $action->hasBody() ) {
                $body = $action->getBody();
            }
            else if ( $this->isActionBodyAutoGenerate() ) {
                if ( $actionName == 'add' ) {
                    $item = $this->getActionBodyAutoGenerateStore();
                    if ( $item != null ) {
                        $store = $item->getStore();
                        if ( $store != null ) {
                            if ( $action instanceof Clevis_View_Action ) {
                                $body = $store->getId() . '.createEntity();';
                            } else if ( $action instanceof Clevis_View_ActionMenu ) {
                                $body = $store->getId() . '.createEntity({data: data});';
                            }
                        }
                    }
                }
                else if ( $actionName == 'edit' ) {
                    if ( $action instanceof Clevis_View_Action ) {
                        $item = $this->getActionBodyAutoGenerateStore();
                        if ( $item != null ) {
                            $store = $item->getStore();
                            if ( $store != null ) {
                                $body = $store->getId() . '.editEntity(' . $item->getId() . '.getSelectedItem());';
                            }
                        }
                    }
                }
                else if ( $actionName == 'delete' ) {
                    if ( $action instanceof Clevis_View_Action ) {
                        $item = $this->getActionBodyAutoGenerateStore();
                        if ( $item != null ) {
                            $store = $item->getStore();
                            if ( $store != null ) {
                                $body = 'var result = ' . $store->getId() . '.deleteEntity(' . $item->getId() . '.getSelectedItem());';
                                $body .= PHP_EOL . 'if ( result != false && ' . $item->getId() . '.reset != null )' . PHP_EOL . '    ' . $item->getId() . '.reset();';
                            }
                        }
                    }
                }
                else if ( $actionName == 'ok' ) {
                    if ( $action instanceof Clevis_View_Action ) {
                        $dialog = $this->getActionBodyAutoGenerateDialog();
                        if ( $dialog != null ) {
                            $body .= $dialog->getId() . '.endConfirm();';
                        }
                    }
                }
                else if ( $actionName == 'cancel' ) {
                    if ( $action instanceof Clevis_View_Action ) {
                        $dialog = $this->getActionBodyAutoGenerateDialog();
                        if ( $dialog != null ) {
                            $body .= $dialog->getId() . '.endCancel();';
                        }
                    }
                }
                else if ( $actionName == 'help' ) {
                    if ( $action instanceof Clevis_View_Action ) {
                        $helpId = $this->getHelpId();
                        $helpNodeId = $action->getId();

                        $body .= 'require([\'clevis/help\'], function(help) { help.showHelp(\'' . $helpId . '\', \'' . $helpNodeId . '\'); });';
                    }
                }

            }

            if ( $actionName == 'printList' ) {
                if ( $action instanceof Clevis_View_Action ) {
                    $body = 'window.printList();';
                } else if ( $action instanceof Clevis_View_ActionMenu ) {
                    $body = 'window.printList({data: data});';
                }
            }

            if ( $body != '' ) {
                $scriptType->addMethod($name,$params,$body);

            }
        }
    }

    /**
     * Find action set for auto generate
     *
     * @return Clevis_View_ActionSet
     */
    public static function & findActionSetForAutoGenerate(Clevis_View_Item & $item) {
        if ( $item->hasParent() ) {
            $nextItem = $item->getParent()->getNextItem($item);
            if ( $nextItem != null ) {
                // If next item is action set we have found it
                if ( $nextItem instanceof Clevis_View_ActionSet ) {
                    return $nextItem;
                }
            }
            // Else if parent parent is panel we can look next to it
            else if ( $item->getParent() instanceof Clevis_View_Panel ) {
                $panel = $item->getParent();
                if ( $panel->hasParent() ) {
                    $nextItem = $panel->getParent()->getNextItem($panel);
                    while ( $nextItem != null ) {
                        // If next item is action set we have found it
                        if ( $nextItem instanceof Clevis_View_ActionSet ) {
                            return $nextItem;
                        }
                        $nextItem = $panel->getParent()->getNextItem($nextItem);
                    }
                }
            }
        }
        $result = null;
        return $result;
    }

    /**
     * Pre render update items
     * @return void
     */
    public function preRenderUpdateItems() {
        // Remove write actions when don't have permission
        if ( $this->hasPermissionWrite() == false ) {
            for ( $index = 0; $index < count($this->items); $index++ ) {
                $item = $this->items[$index];
                if ( $item instanceof Clevis_View_Action || $item instanceof Clevis_View_ActionMenu ) {
                    $name = $item->getName();
                    if ( $name == 'add' || $name == 'edit' || $name == 'delete' ) {
                        array_splice($this->items, $index, 1);
                        $index--;
                    }
                }
            }
        }
    }

    /**
     * Pre render
     * 
     * @return string
     */
    public function preRender() {
        // Update items
        $this->preRenderUpdateItems();

        // Register to dialog for update
        $dialog = $this->getParentWithType('Clevis_View_Dialog');
        if ( $dialog != null)
            $dialog->addUpdateItem($this);

        // Hide action set if none items are presented
        if ( count($this->items) == 0 ) {
            $this->setStyle('display', 'none');
        }
            
        return parent::preRender();
    }
    
}
