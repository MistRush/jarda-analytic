<?php

/**
 * Class that represents listing panel grid
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_ListingPanelGrid extends Clevis_View_Layout implements Clevis_View_ItemHasStore
{    
    /**
     * Panel grid
     * 
     * @var Clevis_View_Grid
     */
    private $grid;

    /**
     * Panel action set
     *
     * @var Clevis_View_ActionSet
     */
    private $actionSet;
    
    /**
     * Constructor
     *
     * @param $title
     */
    public function  __construct($title, $name = null) {
        parent::__construct();

        if ( $name != null )
            $this->setName($name);
        $this->setIdAutoGenerated(true);
        $this->setItemGroup("listingPanelGrid");

        $panelTop = $this->addPanel(Clevis_View_Layout::POSITION_TOP);
        $panelTop->setClass('clevis-view-listing-top');
        $h2 = $panelTop->addElement('h2', $title);
        // Add help
        $h2->addItem(new Clevis_View_Code(
            Clevis_Help::renderHelpImage($this->getHelpId(), Clevis_Image::ICON_NORMAL, Clevis_Help::FLOAT_RIGHT)
        ));

        $this->grid = $this->addGrid(Clevis_View_Layout::POSITION_CENTER);
        
        $this->actionSet = $this->addActionSet(Clevis_View_Layout::POSITION_BOTTOM, Clevis_View_ActionSet::FILL_MANAGE, 'kategorii');
        $this->actionSet->setShowLabels(false);
    }

    /**
     * Setup store
     *
     * @param array $url
     * @param enum $mode (STORE_MODE_WRITE | STORE_MODE_READ)
     * @return Clevis_View_Store
     */
    public function setupStore($url, $mode = STORE_MODE_WRITE) {
        return $this->grid->setupStore($url, $mode);
    }

    /**
     * Register to parent store
     *
     * @param Clevis_View_Store $storeParent
     * @param array $storeQuery for example array('User_ID' => 'parent.ID')
     */
    public function setupStoreParent(Clevis_View_Store $storeParent, $storeQuery) {
        $this->getStore()->registerToParentStore($storeParent, $storeQuery);
    }

    /**
     * Setup store dialog
     *
     * @param Clevis_View_Dialog $dialog  Dialog for entity creation and editation
     * @param null $defaultValues  Default values for entity creation and editation
     * @param null $url  Url for entity creation, editation and deletation, when is null use url from store
     * @param null $values  Entity values to determine which dialog to use (default mode is only one dialog so entity values can be null)
     * @return void
     */
    public function setupStoreDialog(Clevis_View_Dialog $dialog, $defaultValues = array(), $url = null, $values = null) {
        $this->getStore()->getEntity()->addDialog($dialog, $defaultValues, $url, $values);
    }

    /**
     * Add data item to store
     *
     * @param $dataItem
     * @return void
     */
    public function setupStoreAddDataItem($dataItem) {
        $this->getStore()->addDataItem($dataItem);
    }

    /**
     * Add column to grid
     *
     * @param string $type
     * @param string $field
     * @param string $title
     * @param int $width
     * @return Clevis_View_GridColumn
     */
    public function addColumn($type, $field, $title, $width = null) {
        return $this->grid->addColumn($type, $field, $title, $width);
    }

    /**
     * Get store of grid
     *
     * @return Clevis_View_Store
     */
    public function & getStore() {
        return $this->grid->getStore();
    }

    /**
     * Get grid
     * 
     * @return Clevis_View_Grid
     */
    public function & getGrid() {
        return $this->grid;
    }
}

?>