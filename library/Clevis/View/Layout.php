<?php

/**
 * Class that represents layout control, you can add panels to it and assign
 * them LEFT, TOP, CENTER, etc.
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Layout extends Clevis_View_ItemElementScriptType {

    /** Position Type */
    const POSITION_TOP    = 'top';
    const POSITION_CENTER = 'center';
    const POSITION_BOTTOM = 'bottom';
    const POSITION_LEFT   = 'left';
    const POSITION_RIGHT  = 'right';

    /** Design style */
    const DESIGN_SIDEBAR = 'sidebar';
    const DESIGN_HEADLINE = 'headline';
    
    /**
     * Auto size
     * 
     * @var boolean 
     */
    private $autoSize = false;

    /**
     * Splitter
     * 
     * @var bool
     */
    private $splitter = false;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setClass('clevis-view-borderContainer');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('layout');
        $this->setBaseType('clevis/view/BorderContainer');
        $this->setStyleFullSize();
        $this->setStyle('border','none');
        $this->setStyle('margin','0');
        $this->setStyle('padding','0');
        $this->setAutoSize(true);
        $this->setImplicitPadding(true);
    }
    
    /**
     * Set auto size
     * 
     * @param boolean $autoSize 
     */
    public function setAutoSize($autoSize) {
        $this->autoSize = $autoSize;
    }
    
    /**
     * Is auto size
     * 
     * @return boolean 
     */
    public function isAutoSize() {
        return $this->autoSize;
    }

    /**
     * Set use of implicit padding
     * 
     * @param bool $implicitPadding
     * @return void
     */
    public function setImplicitPadding($implicitPadding) {
        $this->setDojoAttribute("gutters", $implicitPadding);
    }

    /**
     * Set design
     * 
     * @param $design (DESIGN_SIDEBAR | DESIGN_HEADLINE)
     * @return void
     */
    public function setDesign($design) {
        $this->setDojoAttribute('design', $design);
    }

    /**
     * Set splitter
     * 
     * @param bool $splitter
     * @return void
     */
    public function setSplitter($splitter) {
        $this->splitter = $splitter;
    }
    
    /**
     * Add subitem to this item
     *
     * @param Clevis_View_Item $item
     * @param $position
     * @param boolean  $skipParentCheck  Flag if parent check should be skipped
     * @return Clevis_View_Item
     */
    public function addItem(Clevis_View_Item $item, $position = null, $skipParentCheck = false) {
        if ( $position != null ) {
            $item->setDojoAttribute('region', $position);
            if ( $item instanceof Clevis_View_Panel && $position != self::POSITION_CENTER ) {
                $item->setDojoAttribute('doLayout', false);
            }
            if ( $this->splitter == true ) {
                $item->setDojoAttribute('splitter', true);
            }
        }
        
        parent::addItem($item, $skipParentCheck);
        return $item;
    }

    /**
     * Add panel
     *
     * @return Clevis_View_Panel
     */
    public function addPanel($position = null) {
        $panel = $this->addItem(new Clevis_View_Panel(),$position);
        return $panel;
    }

    /**
     * Add grid
     *
     * @return Clevis_View_Grid
     */
    public function addGrid($position = null) {
        $grid = $this->addPanel($position)->addGrid();
        return $grid;
    }

    /**
     * Add tab container
     *
     * @return Clevis_View_TabContainer
     */
    public function addTabContainer($position = null) {
        return $this->addItem(new Clevis_View_TabContainer(),$position);
    }

    /**
     * Add action set
     *
     * @param $fillType automatic fill type
     * @return Clevis_View_ActionSet
     */
    public function addActionSet($position = null,$fillType = null,$name = null) {
        $actionSet = new Clevis_View_ActionSet();
        if ( $fillType != null ) {
            $actionSet->fill($fillType,$name);
        }
        return $this->addItem($actionSet,$position);
    }

    /**
     * On init script type notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptType(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptType($scriptType);
        
        // Attributes
        if ( $this->isAutoSize() ) {
            $scriptType->addField('autoSize', 'true');
        }

        // Init panels
        if ( $this->isAutoSize() ) {
            $initPanelsBody = '';
            $itemIndex = 0;
            foreach ( $this->items as $panel ) {
                if ( empty($initPanelsBody) == false )
                    $initPanelsBody .= PHP_EOL;
                $initPanelsBody .= 'this.panels.push("' . $panel->getId() . '");';
                $itemIndex++;
            }
            $scriptType->addMethod('initPanels',null,$initPanelsBody);
        }
    }

    /**
     * Pre render
     *
     * @return string
     */
    public function preRender() {
        $output = parent::preRender();

        if ( $this->isAutoSize() ) {
            $itemIndex = 0;
            foreach ( $this->items as &$item ) {
                $output .= '<div id="' . $this->getId() . '_panel' . $itemIndex . '" style="display: table; padding: 0; margin: 0;">' . PHP_EOL;
                $output .= $item->render();
                $output .= '</div>' . PHP_EOL;
                $itemIndex++;
            }
        }
        return $output;
    }
    
    /**
     * Render
     *
     * @return string
     */
    public function render() {
        $output = '';

        // Render script
        $output .= $this->renderScript();

        // Render layout
        $output .= $this->renderBeginTag();
        $output .= '<div class="layout-progress-bar"><p>Načítání...</p><div class="layout-progress-bar-image"></div></div>';
        if ( $this->isAutoSize() == false ) {
            $output .= $this->renderContent();
        }
        $output .= $this->renderEndTag();
        
        return $output;
    }

}
?>