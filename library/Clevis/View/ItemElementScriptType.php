<?php

/**
 * Class that represents every item that is element with script with type in view
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_ItemElementScriptType extends Clevis_View_ItemElementScript {

    /**
     * Enable script type
     *
     * @var boolean
     */
    private $enableScriptType = true;
    
    /**
     * Base type
     *
     * @var string
     */
    private $baseType;

    /**
     * Constructor
     */
    public function __construct() {
        parent::__construct();
    }

    /**
     * Enable script type
     *
     * @param boolean $enableScriptType
     */
    public function enableScriptType($enableScriptType) {
        $this->enableScriptType = $enableScriptType;
    }

    /**
     * Is script type enabled
     *
     * @return boolean $enableScriptType
     */
    public function isScriptTypeEnabled() {
        return $this->enableScriptType;
    }

    /**
     * Set base type
     *
     * @param string $baseType
     */
    public function setBaseType($baseType) {
        $this->baseType = $baseType;
        $this->setType(null);
    }

    /**
     * Get base type
     *
     * @return string $baseType
     */
    public function getBaseType() {
        return $this->baseType;
    }

    /**
     * On init script notification
     *
     * @param Clevis_View_Script $script
     */
    protected function onInitScript(Clevis_View_Script & $script) {
        parent::onInitScript($script);
        if ( $this->isScriptTypeEnabled() ) {
            $scriptType = $script->addType($this->getType(),$this->getBaseType());
            $this->onInitScriptType($scriptType);
            $this->onInitScriptTypeMethods($scriptType);
        }
    }

    /**
     * Get data item properties
     * 
     * @param $dataItem
     * @param string $begin
     * @param string $end
     * @return string
     */
    protected function dataItemGetProperties(&$dataItem) {
        $id = 'null';
        $getter = 'null';
        if ( $dataItem instanceof Clevis_View_ItemElement ) {
            // Id is id
            $id = '"' . $dataItem->getId() . '"';
            // Store can by accessed by id
            if ( $dataItem instanceof Clevis_View_OldStore || $dataItem instanceof Clevis_View_Store ) {
                $getter = $dataItem->getId();
            }
            // Other things can by accesed by dijit id
            else {
                $getter = 'registry.byId("' . $dataItem->getId() . '")';
            }
        }
        else {
            $getter = $dataItem;
        }
        // Return result
        $result = array();
        $result['id'] = $id;
        $result['getter'] = 'function(){ return ' . $getter . '; }';
        return $result;
    }
    
    /**
     * On init script type notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptType(Clevis_View_ScriptType & $scriptType) {
        // Data parent must have data items properties
        if ( $this->dataIsParent() ) {
            // Init data items
            $dataInitItemsBody = '';
            foreach ( $this->getDataItems() as $dataItem ) {
                if ( empty($dataInitItemsBody) == false )
                    $dataInitItemsBody .= PHP_EOL;
                $data = $this->dataItemGetProperties($dataItem);
                $dataInitItemsBody .= 'this.dataAddItem(' . PHP_EOL . '  ' . $data['id'] . ',' . PHP_EOL . '  ' . $data['getter'] . PHP_EOL. ');';
            }
            $scriptType->addMethod('dataInitItems',null,$dataInitItemsBody);
        }
    }

    /**
     * On init script type methods notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptTypeMethods(Clevis_View_ScriptType & $scriptType) {
    }

    /**
     * Pre render
     *
     * @return string
     */
    public function preRender() {
        $output = parent::preRender();

        // Auto generate type
        if ( $this->enableScriptType ) {
            $this->checkTypeAutoGenerated();
        }
        
        return $output;
    }

}
?>