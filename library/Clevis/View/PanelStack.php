<?php

/**
 * Class that represents panel stack - it can show only one panel but you can
 * dynamically switch between them
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_PanelStack extends Clevis_View_ItemElementScriptType {

    /** Data attribute */
    private $dataAttribute;
    
    /** Dynamic container */
    private $dynamicContainer;
    
    /** Parent for panels */
    private $panelParent = null;
    
    /** Dynamic Panels */
    private $panels = array();
    
    /**
     * Constructor
     */
    public function  __construct($dataAttribute, Clevis_View_DynamicContainer & $dynamicContainer) {
        $this->dataAttribute = $dataAttribute;
        $this->dynamicContainer = $dynamicContainer;
        parent::__construct();
        $this->setTag('div');
        $this->setClass('clevis-view-panelStack');
        $this->setBaseType('clevis.view.PanelStack');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('panelStack');
    }
    
    /**
     * Set panel parent
     * @param Clevis_View_ItemEleemnt $panelParent 
     */
    public function setPanelParent(Clevis_View_ItemElement & $panelParent) {
        $this->panelParent = &$panelParent;
    }
    
    /**
     * Javascript: onSetPanel
     * Parameters: panel, info
     */
    public function onSetPanel($onSetPanel) {
        $this->onSetPanel = $onSetPanel;
    }
    private $onSetPanel = null;
    
    /**
     * Add panel to stack
     * 
     * @param Clevis_View_Item $panel
     * @param string|array $panelAttributeValue
     * @param string $panelName
     * @return Clevis_View_Panel 
     */
    public function addPanel(Clevis_View_Item $panel, $panelAttributeValue = null, $panelName = null) {
        // Set parent
        if ( $this->panelParent != null )
            $panel->setParent($this->panelParent);
        else
            $panel->setParent($this);
        
        // Add panel
        $this->panels[] = $panel;
        
        $onSetPanel = '';
        if ( $this->onSetPanel != null )
            $onSetPanel = ' ' . $this->onSetPanel;
        
        // Add item to dynamic container
        $this->dynamicContainer->addItem(
            $panel, 
            'var panel = item; if ( visible ) { ' . $this->getId() . '.setPanel(item, info); }',
            array($this->dataAttribute => $panelAttributeValue),
            array('name' => $panelName)        
        );
        
        return $panel;
    }
    
    /**
     * Pre render
     *
     * @return void
     */
    public function preRender() {
        // Set before update
        $this->dynamicContainer->onBeforeUpdate($this->getId() . '.hidePanel();');
        // Set after update
        $this->dynamicContainer->onAfterUpdate($this->getId() . '.checkPanel();');

        if ( $this->onSetPanel != null )
            $this->setDojoAttribute('onSetPanel', $this->onSetPanel);
        
        $output = parent::preRender();
        
        // Pre render all panels
        foreach ( $this->panels as $panel ) {
            $output .= $panel->preRender();
        }
        // Render all panels
        foreach ( $this->panels as $panel ) {
            $output .= $panel->render();
        }
        
        return $output;
    }

    public function render() {
        $output = '<div>';
        $output .= parent::render();
        $output .= '</div>';
        return $output;
    }

}
?>