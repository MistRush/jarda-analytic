<?php

/**
 * Class that represents checkgroup
 *
 * @author Pavel Cihlar
 */
class Clevis_View_CheckGroup extends Clevis_View_Container {

    /**
     * Constructor
     */
    public function  __construct($title = null) {
        parent::__construct();
        $this->setClass('clevis-view-checkGroup');
        $this->setHelpIdRequired(true);
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('checkGroup');
        $this->enableScript(true);
        $this->enableScriptType(true);
        $this->setBaseType('clevis/view/CheckGroup');

        // Make data connection layer
        $this->dataSetIsItem(true);
        $this->dataSetIsParent(true);

        if ( $title != null )
            $this->setTitle($title);
    }

    /**
     * Set title
     * 
     * @param string $title
     * @return void
     */
    public function setTitle($title) {
        $this->setDojoAttribute('title', $title);
    }

    /**
     * Get title
     *
     * @return string $title
     */
    public function getTitle() {
        return $this->getAttribute('title');
    }

    /**
     * Set data attribute
     *
     * @param string $dataAttribute
     */
    public function setDataAttribute($dataAttribute) {
        if ( $this->hasName() == false )
            $this->setName(str_replace('.','_',$dataAttribute));
        $this->setDojoAttribute('dataAttribute',$dataAttribute);
    }

    /**
     * Render
     * 
     * @return string
     */
    public function render() {
        $legendId = $this->getId() . '_legend';
        $contentId = $this->getId() . '_content';

        $output = $this->renderScript();
        $output .= $this->renderBeginTag('fieldset');
        $output .= '<legend id="' . $legendId .'" jsId="' . $legendId . '">' . Clevis_Help::renderHelpImage($this->getHelpId(), Clevis_Image::ICON_SMALL) . '</legend>' . PHP_EOL;
        $output .= '<div id="' . $contentId . '" jsId="' . $contentId . '" dojoType="dijit.layout.ContentPane" class="clevis-view-checkGroup-content">' . PHP_EOL;
        $output .= $this->renderContent();
        $output .= '</div>' . PHP_EOL;
        $output .= $this->renderEndTag();

        return $output;
    }

}
?>