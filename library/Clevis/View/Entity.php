<?php

/**
 * Class for manipulating with entities in page (create, update, delete).
 * It can show dialogs for add/edit operation, etc.
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Entity extends Clevis_View_ItemElementScriptType {

    const ALLOW_NULL = 'allow_null';

    /**
     * Entity unique identifier
     */
    private $identifier;

    /**
     * Entity default controller url
     */
    private $controllerUrl;

    /**
     * Dialogs for editing
     *
     * @var Clevis_View_Dialog
     */
    private $dialog = array();
   
    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('checkGroup');
        $this->setBaseType('clevis/view/Entity');
    }

    /**
     * Set controller url
     *
     * @param $controllerUrl
     */
    public function setControllerUrl($controllerUrl, $format = true) {
        if ($format) {
            $controllerUrl = Clevis_Helper::formatUrl($controllerUrl);
        }
        $this->controllerUrl = $controllerUrl;
    }

    /**
     * Has controller url
     *
     * @return boolean
     */
    public function hasControllerUrl() {
        return $this->controllerUrl != null;
    }

    /**
     * Get controller url
     *
     * @return $controllerUrl
     */
    public function getControllerUrl() {
        return $this->controllerUrl;
    }

    /**
     * Set identifier
     *
     * @param string $identifier
     */
    public function setIdentifier($identifier) {
        $this->identifier = $identifier;
    }

    /**
     * Has identifier
     *
     * @return boolean
     */
    public function hasIdentifier() {
        return $this->identifier != null;
    }

    /**
     * Get identifier
     *
     * @return string $identifier
     */
    public function getIdentifier() {
        return $this->identifier;
    }

    /**
     * Add entity dialog
     *
     * @param Clevis_View_Dialog|string $dialog  Dialog for entity creation and editation (or its id)
     * @param array $defaultValues  Default values for entity creation and editation
     * @param string|null $url  Url for entity creation, editation and deletation, when is null use url from store
     * @param array|null $values  Entity values to determine which dialog to use (default mode is only one dialog so entity values can be null)
     * @param boolean $addItem  Flag if $dialog should by added by addItem to render tree
     * @return Clevis_View_Dialog
     */
    public function addDialog($dialog, $defaultValues = array(), $url = null, $values = null, $addItem = true) {
        $this->dialog[] = array(
            'dialog' => $dialog,
            'defaultValues' => $defaultValues,
            'url' => $url,
            'values' => $values
        );
        // Add dialog as child to render tree
        if ( $addItem && !is_string($dialog) ) {
            $this->addItem($dialog);
        }
        return $dialog;
    }

    /**
     * Set to show edit dialog on load
     *
     * @param boolean $debugShowDialog
     */
    public function setDebugOnLoadEdit($entityIdentifier) {
        if ( $entityIdentifier != null )
            $this->setDojoAttribute('debugOnLoadEdit',$entityIdentifier);
        else
            $this->unsetAttribute('debugOnLoadEdit');
    }

    /**
     * Format dialog function to get default filed values
     *
     * @param array $defaultValues
     */
    public function formatDialogFunctionDefaultFieldValues($defaultValues) {
        $function = 'var value = null;' . PHP_EOL;
        if ( $defaultValues != null ) {
            foreach ( $defaultValues as $field => $value ) {
                $allowNull = false;
                if ( is_array($value) ) {
                    $allowNull = array_key_exists(1, $value) ? ($value[1] == Clevis_View_Entity::ALLOW_NULL) : false;
                    $value = $value[0];
                }
                if ( is_integer($value) ) {
                    $value = $value;
                } else if (is_string($value) ) {
                    $value = $value;
                } else if ( $value instanceof Clevis_View_CurrentValueAble ) {
                    $value = $value->getCurrentValue();
                } else if ( $value == null ) {
                    $value = 'null';
                } else {
                    throw new Exception('Field default value has unknown type. Available types are: (string | Clevis_View_CurrentValueAble)');
                }

                $function .= 'value = ' . $value . ';' . PHP_EOL;
                if ( $allowNull == false ) {
                    $function .= 'if ( value == null )' . PHP_EOL;
                    $function .= '    return false;' . PHP_EOL;
                }
                $function .= 'data["' . $field . '"] = value;' . PHP_EOL;
            }
        }
        $function .= 'return true;';
        return $function;
    }

    /**
     * On init script type notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptType(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptType($scriptType);
        // Add fields
        if ( $this->hasIdentifier() )
            $scriptType->addField('identifier','"' . $this->getIdentifier() . '"');
        if ( $this->hasControllerUrl() )
            $scriptType->addField('controllerUrl','"' . $this->getControllerUrl() . '"');

        // Init dialog array method
        $initDialogBody = '';
        foreach ( $this->dialog as $dialog) {
            if ( $initDialogBody != '' ) {
                $initDialogBody .= PHP_EOL;
            }
            $dialogId = $dialog['dialog'];
            if ( !is_string($dialogId) ) {
                $dialogId = $dialogId->getId();
            }
            $initDialogBody .= 'this.dialog.push({' . PHP_EOL;
            $initDialogBody .= '  getDialog: function(){ return ' . $dialogId . ';},' . PHP_EOL;
            $initDialogBody .= '  url: "' . $dialog['url'] . '",' . PHP_EOL;
            $initDialogBody .= '  setDefaultValues: function(data){' . PHP_EOL;
            $initDialogBody .= Clevis_View_Code::indentHtmlCode($this->formatDialogFunctionDefaultFieldValues($dialog['defaultValues']), 2) . PHP_EOL;
            $initDialogBody .= '  },' . PHP_EOL;
            $initDialogBody .= '  values: ' . json_encode($dialog['values']) . '' . PHP_EOL;
            $initDialogBody .= '});';
        }
        $scriptType->addMethod('initDialog', null, $initDialogBody);
    }

    /**
     * On init script type methods notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptTypeMethods(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptTypeMethods($scriptType);
    }

    /**
     * Pre render store
     *
     * @return string
     */
    public function preRender() {
        foreach ( $this->dialog as &$dialog) {
            if ( $dialog['url'] == null ) {
                $dialog['url'] = $this->controllerUrl;
            } else {
                $dialog['url'] = Clevis_Helper::formatUrl($dialog['url'], false);
            }
        }
        return parent::preRender();
    }

}
?>