<?php

/**
 * Class that represents form
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Form extends Clevis_View_Form_Container {

    /**
     * Form for render
     *
     * @var Zend_Form
     */
    private $form;

    /**
     * Columns of form table
     *
     * @var integer
     */
    private $columns = 1;

    /**
     * Map of items by it's name
     *
     * @var array
     */
    private $itemByName = array();

    /**
     * Skip Table Style
     *
     * @boolean $skipTableStyle
     */
    private $skipTableStyle = false;

    /**
     * Constructor
     */
    public function __construct() {
        parent::__construct();
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('form');
        $this->setType('clevis.view.Form');
        $this->setClass('clevis-view-form');
        // Form is always data item (it will connect to first data parent)
        $this->dataSetIsItem(true);
    }

    /**
     * Set form
     *
     * @param Zend_Form $form
     */
    public function setForm(Zend_Form & $form) {
        $this->form = &$form;
    }

    /**
     * Has form
     *
     * @return boolean
     */
    public function hasForm() {
        return $this->form != null;
    }

    /**
     * Get form
     *
     * @return $form
     */
    public function & getForm() {
        return $this->form;
    }

    /**
     * Set form
     *
     * @param Boolean $skipTableStyle
     */
    public function setSkipTableStyle($skipTableStyle) {
        $this->skipTableStyle = $skipTableStyle;
    }

    /**
     * Get form
     *
     * @return $skipTableStyle
     */
    public function getSkipTableStyle() {
        return $this->skipTableStyle;
    }

    /**
     * Set data attribute
     *
     * @param string $dataAttribute
     */
    public function setDataAttribute($dataAttribute) {
        $this->setDojoAttribute('dataAttribute', $dataAttribute);
    }

    /**
     * Has data attribute
     *
     * @return boolean
     */
    public function hasDataAttribute() {
        return $this->hasAttribute('dataAttribute');
    }

    /**
     * Get data attribute
     *
     * @return $dataAttribute
     */
    public function getDataAttribute() {
        return $this->getAttribute('dataAttribute');
    }

    /**
     * Set columns
     *
     * @param integer $columns
     */
    public function setColumns($columns) {
        $this->columns = $columns;
    }

    /**
     * Get columns
     *
     * @return $columns
     */
    public function getColumns() {
        return $this->columns;
    }

    /**
     * Add item to form
     *
     * @param Clevis_View_Item $item
     * @return Clevis_View_Item
     */
    public function addItem(Clevis_View_Item $item, $skipParentCheck = false) {
        if ($item instanceof Clevis_View_Form_Item)
            $this->itemByName[$item->getName()] = $item;
        return parent::addItem($item);
    }

    /**
     * Get item by name
     *
     * @param string $name
     * @return Clevis_View_Item
     */
    public function getItemByName($name) {
        return $this->itemByName[$name];
    }

    /**
     * Render form
     *
     * @return string
     */
    public function render() {
        $output = '';
        if ($this->hasForm()) {
            $form = $this->getForm();
            $form->setAttrib('id', $this->getId());
            $form->setAttrib('jsId', $this->getId());
            $output .= $form->render();
        } else {
            $output = $this->renderBeginTag('form');


            $itemCounter = 0;
            $itemColumnSpan = 0;

            $outputBefore = '';
            $outputContent = '';
            foreach ($this->items as &$item) {
                if ($item instanceof Clevis_View_Form_Item) {
                    $itemId = $item->getId();
                    if ($itemColumnSpan == 0) {
                        if ($itemCounter % $this->getColumns() == 0 && $itemCounter != 0) {
                            $outputContent .= '    </tr>' . PHP_EOL;
                            $outputContent .= '    <tr class="trForm">' . PHP_EOL;
                        }
                        $outputContent .= '      <td class="tdFormLabel">' . PHP_EOL;
                        $outputContent .= '        <label id="' . $itemId . '_label" class="labelForm" for="' . $itemId . '">' . $item->getLabel() . '</label>' . PHP_EOL;
                        $outputContent .= '      </td>' . PHP_EOL;
                        $itemCounter++;
                    }

                    if ($itemColumnSpan == 0) {
                        $outputContent .= '      <td class="tdForm">' . PHP_EOL;
                    }

                    if ($item->getLabel() != null) {
                        //$outputContent .= Clevis_Help::renderHelpImage($item->getHelpId(), Clevis_Image::ICON_SMALL, Clevis_Help::FLOAT_RIGHT);
                    }

                    $outputContent .= Clevis_View_Code::indentHtmlCode($item->render(), 4);
                    $afterText = $item->getAfterText();
                    if ($afterText != null)
                        $outputContent .= '&nbsp;<span class="textForm">' . $afterText . '</span>';
                    $outputContent .= PHP_EOL;

                    if ($itemColumnSpan > 0) {
                        $itemColumnSpan--;
                    } else if ($item->getColumnSpan() != null) {
                        $itemColumnSpan = $item->getColumnSpan();
                    }

                    if ($itemColumnSpan == 0) {
                        $outputContent .= '      </td>' . PHP_EOL;
                    }
                } else {
                    if ($item instanceof Clevis_View_Code) {
                        $outputContent .= '    </tr>' . PHP_EOL;
                        $outputContent .= '    <tr class="trForm">' . PHP_EOL;
                        $outputContent .= '      <td class="tdFormLable"></td>' . PHP_EOL;
                        $outputContent .= '      <td class="tdForm">' . PHP_EOL;
                        $outputContent .= '      ' . $item->render() . PHP_EOL;
                        $outputContent .= '      </td>' . PHP_EOL;
                        $itemCounter++;
                    } else {
                        $outputBefore .= Clevis_View_Code::indentHtmlCode($item->render(), 4) . PHP_EOL;
                    }
                }
            }

            $output .= $outputBefore;

            if ($this->skipTableStyle)
                $output .= '  <table>' . PHP_EOL;
            else
                $output .= '  <table class="tableForm">' . PHP_EOL;
            $output .= '    <tr class="trForm">' . PHP_EOL;
            $output .= $outputContent;
            $output .= '    </tr>' . PHP_EOL;
            $output .= '  </table>' . PHP_EOL;
            $output .= $this->renderEndTag();
        }

        return $output;
    }

}

?>