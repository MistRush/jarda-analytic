<?php

/**
 * Class that represents action
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Action extends Clevis_View_ItemElement {
  
    /**
     * Action title
     *
     * @var string
     */
    private $title;

    /**
     *
     * Action body
     *
     * @var string
     */
    private $body;

    /**
     *
     * Action for context menu only
     *
     * @var string
     */
    private $contextMenuOnly = false;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('button');
        $this->setType('clevis.view.form.Button');
        $this->setIdAutoGenerated(true);
        $this->setElementAttribute('focused', true);
    }

    /**
     * Set action title
     *
     * @param string $title
     * @return void
     */
    public function setContextMenuOnly($contextMenuOnly) {
        $this->contextMenuOnly = $contextMenuOnly;
    }

    /**
     * Get action title
     *
     * @return $title
     */
    public function getContextMenuOnly() {
       return $this->contextMenuOnly;
    }

    /**
     * Set action title
     *
     * @param string $title
     * @return void
     */
    public function setTitle($title) {
        $this->title = $title;
    }

    /**
     * Get action title
     *
     * @return $title
     */
    public function getTitle() {
       return $this->title;
    }

    /**
     * Set action image
     *
     * @param string $image
     * @return void
     */
    public function setImage($image) {
        $this->setDojoAttribute('iconClass', $image);
    }

    /**
     * Has action image
     *
     * @return boolean
     */
    public function hasImage() {
       return $this->hasDojoAttribute('iconClass') != null;
    }

    /**
     * Get action image
     *
     * @return $image
     */
    public function getImage() {
       return $this->getDojoAttribute('iconClass');
    }

    /**
     * Set action body
     *
     * @param string $body
     * @return void
     */
    public function setBody($body) {
        $this->body = $body;
    }

    /**
     * Has action body
     *
     * @return boolean
     */
    public function hasBody() {
       return $this->body != null;
    }

    /**
     * Get action body
     *
     * @return $body
     */
    public function getBody() {
       return $this->body;
    }

    /**
     * Set enabled
     *
     * @param $enabled
     * @return void
     */
    public function setEnabled($enabled) {
        $this->setDojoAttribute('disabled', true);
    }

    /**
     * Get handler
     *
     * @return string
     */
    public function getHandler() {
        return $this->getParent()->getId() . '.onButton' . ucfirst($this->getName()) . '()';
    }

    /**
     * Render toolTip
     *
     * @return string
     */
    public function renderToolTip() {
        $toolTip = new Clevis_View_ToolTip();
        $toolTip->setContent($this->getTitle());
        $toolTip->setPosition('above');
        $toolTip->setConnectId($this->getId());

        return $toolTip->render();
    }

    /**
     * Render action
     *
     * @return string
     */
    public function render() {
        if ( $this->getContextMenuOnly() == true )
            return;

        $attributes = array();
        $attributes['onClick'] = $this->getHandler();
        if ( $this->getParent()->isShowLabels() == false )
            $attributes['showLabel'] = 'false';

        $output = '';
        $output .= $this->renderBeginTag('button',$attributes);
        $output .= $this->getTitle();
        $output .= $this->renderEndTag();

        if ( $this->getParent()->isShowLabels() == false ) {
            $output .= $this->renderToolTip();
        }

        return $output;
    }
}
?>