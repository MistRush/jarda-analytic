<?php

/**
 * Class that represents filter set
 *
 * @author Pavel Cihlar
 */

class Clevis_View_FilterSet extends Clevis_View_Form {
    
    /** Filter type */
    const FILTER_TEXT     = 'text';
    const FILTER_NUMBER   = 'number';
    const FILTER_CURRENCY = 'currency';
    const FILTER_DATE     = 'date';
    const FILTER_TIME     = 'time';
    const FILTER_CHECK    = 'check';
    const FILTER_ENUM     = 'enum';
    const FILTER_COMBO    = 'combo';
    const FILTER_RANGE    = 'range';
    
    /** Filter range */
    const FILTER_RANGE_TEXT = 'range:text';
    const FILTER_RANGE_CURRENCY = 'range:currency';
    const FILTER_RANGE_DATE = 'range:date';

    /**
     * Filter target
     *
     * @var string
     */
    private $filterTarget;

    /**
     * Array of filter items
     * 
     * @var array
     */
    private $filterItems = array();

    /**
     * Array of actions
     *
     * @var array
     */
    private $actions = array();

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setClass('clevis-view-filterSet');
        $this->setTag('div');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('filterSet');
        $this->setBaseType('clevis/view/FilterSet');
        $this->enableScript(true);
        $this->enableScriptType(true);
        $this->setColumns(2);
    }

    /**
     * Set filter target
     *
     * @param string $grid
     * @return void
     */
    public function setFilterTarget($filterTarget) {
        $this->filterTarget = $filterTarget;
    }

    /**
     * Get filter target
     *
     * @return $grid
     */
    public function getFilterTarget() {
       return $this->filterTarget;
    }

    /**
     * Add filter item
     *
     * @param string $type
     */
    private function & addFilterItem($type,$attribute = null) {
         switch ( $type ) {
            case self::FILTER_TEXT:
                $textBox = new Clevis_View_Form_TextBox();
                $textBox->setIdAutoGenerated(true);
                $textBox->setItemGroup('filter');
                if ( $attribute != null && array_key_exists('validator-type', $attribute) )
                    $textBox->setValidatorType($attribute['validator-type']);
                return $textBox;

            case self::FILTER_NUMBER:
                $textBox = new Clevis_View_Form_TextBox();
                $textBox->setIdAutoGenerated(true);
                $textBox->setItemGroup('filter');
                $textBox->setValidatorType(VALIDATOR_NUMERIC);
                return $textBox;

            case self::FILTER_CURRENCY:
                $currencyBox = new Clevis_View_Form_CurrencyBox();
                $currencyBox->setIdAutoGenerated(true);
                $currencyBox->setItemGroup('filter');
                if ( $attribute != null && array_key_exists('places',$attribute) )
                    $currencyBox->setPlaces($attribute['places']);
                if ( $attribute != null && array_key_exists('value-type',$attribute) )
                    $currencyBox->setValueType($attribute['value-type']);
                return $currencyBox;

            case self::FILTER_DATE:
                $dateBox = new Clevis_View_Form_DateBox();
                $dateBox->setIdAutoGenerated(true);
                $dateBox->setItemGroup('filter');
                return $dateBox;

            case self::FILTER_TIME:
                $timeBox = new Clevis_View_Form_TimeBox();
                $timeBox->setIdAutoGenerated(true);
                $timeBox->setItemGroup('filter');
                return $timeBox;

            case self::FILTER_CHECK:
                $comboBox = new Clevis_View_Form_ComboBox();
                $comboBox->setIdAutoGenerated(true);
                $comboBox->setItemGroup('filter');
                $comboBox->addDataItem(null, 'Vše');
                $comboBox->addDataItem('1', 'Ano');
                $comboBox->addDataItem('0', 'Ne');
                return $comboBox;

            case self::FILTER_ENUM:
                $comboBox = new Clevis_View_Form_ComboBox();
                $comboBox->setIdAutoGenerated(true);
                $comboBox->setItemGroup('filter');
                $comboBox->addDataItems($attribute['enum-values']);
                return $comboBox;

            case self::FILTER_COMBO:
                $store = new Clevis_View_Store();
                $store->setDynamic(false);
                $store->setReadOnly(true);
                $store->setUrl($attribute['url']);
                $store->addDataItem(array('ID' => null,$attribute['field'] => 'Vše'));
                $this->addItem($store);

                $comboBox = new Clevis_View_Form_ComboBox();
                $comboBox->setIdAutoGenerated(true);
                $comboBox->setItemGroup('filter');
                $comboBox->setStore($store,$attribute['field']);
                return $comboBox;
        }
    }

    /**
     * Add filter
     *
     * @param string $name
     * @param string $type
     * @param string $title
     */
    public function addFilter($field,$type,$title,$attribute = null) {
        $widget = '';
        $typeArray = explode(':',$type);
        switch ( $typeArray[0] ) {
            case self::FILTER_RANGE:
                $filterItemBegin = $this->addItem($this->addFilterItem($typeArray[1],$attribute));
                $filterItemBegin->setColumnSpan(1);
                $filterItemBegin->setLabel('<div style="position: absolute; margin-top: -22px; width: 145px;">' . $title . ' od</div>');
                $filterItemBegin->setName($field . 'From');
                $filterItemBegin->setAfterText('<div style="position: absolute; margin-left: -26px; margin-top: 5px;">do</div>');
                $filterItemEnd = $this->addItem($this->addFilterItem($typeArray[1],$attribute));
                $filterItemEnd->setName($field . 'To');
                $widget = '["' . $filterItemBegin->getId() . '","' . $filterItemEnd->getId() . '"]';
                break;
            
            default:
                $filterItem = $this->addItem($this->addFilterItem($typeArray[0],$attribute));
                $filterItem->setName($field);
                $filterItem->setLabel($title);
                $widget = '"' . $filterItem->getId() . '"';
                break;
        }
        $this->filterItems[] = array('field' => $field,'type' => $type,'title' => $title,'widget' => $widget);
    }

    /**
     * Add action
     *
     * @param string $name
     * @param string $title
     * @param string $image
     * @param string $body
     * @return Clevis_View_Action
     */
    public function addAction($name, $title, $image = null, $body = null) {
        $this->actions[] = array('name' => $name, 'title' => $title, 'image' => $image, 'body' => $body);
    }

    /**
     * On init script type notification
     *
     * @param Clevis_View_ScriptType $scriptType
     */
    protected function onInitScriptType(Clevis_View_ScriptType & $scriptType) {
        parent::onInitScriptType($scriptType);

        // Get filter target
        $filterTarget = $this->getFilterTarget();
        if ( $filterTarget instanceof Clevis_View_ItemElement )
            $filterTarget = $filterTarget->getId();
        $scriptType->addMethod('getFilterTarget',null,'return ' . $filterTarget . ';');

        // Init filter items
        $initFilterItemsBody = '';
        foreach ( $this->filterItems as $filterItem ) {
            if ( empty($initFilterItemsBody) == false )
                $initFilterItemsBody .= PHP_EOL;
            $initFilterItemsBody .= 'this.filterItems.push({';
            $initFilterItemsBody .= 'field: "' . $filterItem['field'] .'",';
            $initFilterItemsBody .= 'type: "' . $filterItem['type'] .'",';
            $initFilterItemsBody .= 'widget: ' . $filterItem['widget'];
            $initFilterItemsBody .= '});';
        }
        $scriptType->addMethod('initFilterItems',null,$initFilterItemsBody);
    }

    /**
     * Render filter set
     *
     * @return string
     */
    public function render() {
        $output = '';

        // Render parent
        $output .= '<div' . $this->renderAttributes() . '>' . PHP_EOL;
        $output .= parent::renderScript();
        $output .= parent::render();

        // Actions
        $actionSet = new Clevis_View_ActionSet();
        $actionSet->addAction('filter','Filtrovat','image-filter')->setBody($this->getId() . '.performFilter();');
        $actionSet->addAction('reset','Vymazat filtr.','image-cancel')->setBody($this->getId() . '.performReset();');
        foreach ( $this->actions as $action ) {
            $actionSet->addAction($action['name'], $action['title'], $action['image'], $action['body']);
        }
        $actionSet->setAlign(ALIGN_RIGHT);
        $actionSet->setElementAttribute('style','margin: 0px 5px 5px 5px; padding: 0px; border: 0px; background: none;');
        $output .= '<div style="height: auto; width: 100%; text-align: right;">' . $actionSet->render() . '</div>';

        // End
        $output .= '</div>' . PHP_EOL;

        return $output;
    }
}
?>