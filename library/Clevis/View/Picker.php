<?php

/**
 * Data Picker
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_Picker extends Clevis_View_ItemElementScript implements Clevis_View_CurrentValueAble, Clevis_View_ItemHasStore {
    
    /**
     * Store of picker
     * 
     * @var Clevis_View_Store
     */
    private $store;
    
    /**
     * Chooser grid
     * 
     * @var type 
     */
    private $grid;

    /**
     * Picker ActionSet
     *
     * @var Clevis_View_ActionSet
     */
    private $actionSet;
    
    /**
     * Constructor
     */
    public function __construct() {
        $this->setType('clevis.view.Picker');
        $this->setTag('div');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('picker');        
        $this->setClass('clevis-view-picker');
        $this->setEmptyText('Prazdná data');
        
        // Create chooser grid and action set
        $this->grid = new Clevis_View_Grid();
        $this->actionSet = new Clevis_View_ActionSet();
        $this->actionSet->setParent($this);
        $this->actionSet->setActionBodyAutoGenerateStore($this);
        $this->actionSet->setShowLabels(false);
        $this->actionSet->addClass('clevis-view-picker-actionSet');
    }
    
    /**
     * Set empty text
     * 
     * @param string $emptyText 
     */
    public function setEmptyText($emptyText) {
       $this->setDojoAttribute('emptyText', $emptyText);
    }
    
    /**
     * Get empty text
     * 
     * @return string
     */
    public function getEmptyText() {
        return $this->getDojoAttribute('emptyText');
    }
    
    /**
     * Bind picker to dynamic param
     * 
     * @param string $dynamicParam
     */
    public function bindDynamicParam($dynamicParam) {
        Clevis_DynamicUrl::bindDynamicParam($this, $dynamicParam);
    }
    
    /**
     * Set store
     *
     * @param Clevis_View_Store $store
     * @param string $storeField  Store field or format for display name (can be 'Name' or '[Name] ([ID])')
     * @param string $storeFieldFormat  Array of store field formats ie (array('Type' => array('1' => 'XXX', '2' => 'YYY')))
     * @return void
     */
    public function setStore(Clevis_View_Store & $store, $storeField, $storeFieldFormat = null) {
        if ( $this->store != null )
            $store->removeConnectedItem($this);
        $this->store = $store;
        $this->setDojoAttribute('storeField', $storeField);
        if ( $storeFieldFormat != null )
            $this->setDojoAttribute('storeFieldFormat', json_encode($storeFieldFormat));
        if ( $store != null )
            $store->addConnectedItem($this);
    }

    /**
     * Has store
     * 
     * @return boolean
     */
    public function hasStore() {
        return $this->store != null;
    }

    /**
     * Get store of picker
     *
     * @return Clevis_View_Store $store
     */
    public function & getStore() {
        return $this->store;
    }
    
     /**
     * Add column to grid
     * 
     * @param string $type
     * @param string $field
     * @param string $title
     * @param int $width
     * @return Clevis_View_GridColumn
     */
    public function addChooserColumn($type, $field, $title, $width = null) {
        return $this->grid->addColumn($type, $field, $title, $width);
    }

    /**
     * Add action
     *
     * @param string $name
     * @param string $title
     * @param string $image
     * @return Clevis_View_Action
     */
    public function addAction($name,$title,$image = null,$body = null) {
        return $this->actionSet->addAction($name,$title, $image, $body);
    }

    /**
     * Add actions by fill type
     *
     * @param enum $fillType action set fill type
     * @param $name action name to add to title
     * @return void
     */
    public function addActions($fillType, $name = null) {
        $this->actionSet->fill($fillType, $name);
    }
    
    /**
     * Get current value
     * 
     * @return string
     */
    public function getCurrentValue() {
        return $this->getId() . '.getIdentifier()';
    }
    
    /**
     * Pre render picker
     * 
     * @return string
     */
    public function preRender() {
        $output = '';
        
        if ( $this->actionSet->getItemCount() > 0 && $this->hasPermissionWrite() )
            $output .= $this->actionSet->preRender();
        else
            $this->actionSet = null;
        
        // Set store to grid
        $this->grid->setStore($this->getStore());
        
        // Create chooser
        $chooser = new Clevis_View_ElementDiv();
        $chooser->setId($this->getId() . '_chooser');
        $chooser->setStyleSize(300, 300);
        $chooser->addItem($this->grid);
        
        // Render chooser
        $output .= $chooser->render();
        
        $output .= parent::preRender();
        
        return $output;
    }
    
    /**
     * Render picker
     * 
     * @return string
     */
    public function render() {
        $output = '';
        
        $output .= $this->renderScript();
        $output .= $this->renderBeginTag();
        
        $output .= '<div class="clevis-view-picker-content">' . $this->getEmptyText() . '</div>';
        $output .= '[<a onClick="' . $this->getId() . '.pick();">Vybrat</a>]';
        
        $output .= $this->renderEndTag();

        if ( $this->actionSet != null )
            $output .= $this->actionSet->render();
        
        return $output;
    }

}

?>