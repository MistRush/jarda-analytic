<?php

/**
 * Class that represents store set
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_StoreSet extends Clevis_View_OldStore {

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('storeSet');
        $this->setBaseType('clevis/view/StoreSet');
    }

    /**
     * Register this store to parent data if some exists
     *
     * @param string $dataAttribute which sub data to use
     * @param string $dataColumn which column of data use for set
     */
    public function registerToParentData($dataAttribute,$dataColumn) {
        $this->setDojoAttribute('dataAttribute',$dataAttribute);
        $this->setDojoAttribute('mapAttribute',$dataColumn);
    }

    /**
     * Check if store write want to be registered to parent data
     *
     * @return boolean
     */
    public function isRegisteredToParentData() {
        return $this->hasAttribute('dataAttribute');
    }

    /**
     * Pre render item
     *
     * @return void
     */
    public function preRender() {
        // Register to parent data
        if ( $this->isRegisteredToParentData() ) {
            // Store is data item (it will connect to first data parent)
            $this->dataSetIsItem(true);
        }
        
        parent::preRender();
    }

}
?>