<?php

/**
 * Context Menu
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View_ContextMenu extends Clevis_View_ItemElement {

    /**
     * Manages if context menu is auto generate
     *
     * @var boolean
     */
    private $autoGenerate = true;

    /**
     * ActionSet for auto generate
     *
     * @var Clevis_View_Grid
     */
    private $autoGenerateActionSet;

    /**
     * Constructor
     */
    public function  __construct() {
        parent::__construct();
        $this->setTag('div');
        $this->setType('clevis.view.ContextMenu');
        $this->setIdAutoGenerated(true);
        $this->setItemGroup('checkGroup');
        $this->setStyleHidden();
        $this->setClass('clevis-image-icon-small');
    }

    /**
     * Set context menu auto generate
     *
     * @param boolean $contextMenuAutoGenerate
     * @return void
     */
    public function setAutoGenerate($autoGenerate) {
        $this->autoGenerate = $autoGenerate;
    }

    /**
     * Is context menu auto generate
     *
     * @return boolean $autoGenerate
     */
    public function isAutoGenerate() {
       return $this->autoGenerate;
    }

    /**
     * Set action set for on auto generate
     *
     * @param Clevis_View_ActionSet $autoGenerateActionSet
     * @return void
     */
    public function setAutoGenerateActionSet(Clevis_View_ActionSet & $autoGenerateActionSet) {
        $this->autoGenerateActionSet = $autoGenerateActionSet;
    }

    /**
     * Get action set for auto generate
     *
     * @return Clevis_View_ActionSet $autoGenerateActionSet
     */
    public function & getAutoGenerateActionSet() {
        return $this->autoGenerateActionSet;
    }

    /**
     * Add context menu item
     *
     * @param string $title
     * @param string $code
     */
    public function addContextMenuItem($name,$title,$image = null,$action = null) {
        $item = new Clevis_View_MenuItem();
        $item->setName($name);
        $item->setLabel($title);
        $item->setImage($image);
        $item->setAction($action);
        return $this->addItem($item);
    }

    /**
     * Pre render
     *
     * @return void
     */
    public function preRender() {
        parent::preRender();

        // Auto generate context menu
        if ( $this->isAutoGenerate() ) {
            $actionSet = $this->getAutoGenerateActionSet();
            if ( $actionSet != null ) {
                foreach ( $actionSet->items as $action ) {
                    if ( $action instanceof Clevis_View_Action ) {
                        $title = null;

                        // We can't auto connect text to title because user can set that by hand
                        //if ( $action->getName() == "add" )
                        //    $title = " nový záznam";
                        //if ( $action->getName() == "edit" || $action->getName() == "delete" )
                        //    $title = " vybraný záznam";
                        
                        $this->addContextMenuItem($action->getName(),$action->getTitle() . $title,$action->getImage(),$action->getHandler());
                    } else if ( $action instanceof Clevis_View_ActionMenu ) {
                        $title = null;
                        if ( $action->getName() == "add" )
                            $title = " nový záznam";

                        $menuItem = $this->addContextMenuItem($action->getName(),$action->getTitle() . $title,$action->getImage());
                        for ( $index = 0; $index < $action->getItemCount(); $index++ ) {
                            $item = $action->getItem($index);
                            $menuItem->addMenuItem($item->getLabel(),$item->getImage(),$action->getMenuItemHandler($item));
                        }
                    }
                }
            }
        }
    }
}
?>