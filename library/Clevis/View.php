<?php

/**
 * Class for rendering view
 *
 * @author Martin Srom <srom.martin@gmail.com>
 */
class Clevis_View extends Clevis_View_Container {

    /**
     * @var Zend_View
     */
    private $zendView;
    
    /**
     * Page fullsize
     * 
     * @var boolean
     */
    private $pageFullSize = false;
    
    /**
     * Constructor
     *
     * @return void
     */
    public function __construct($name = null) {
        parent::__construct();
        if ( $name != null )
            $this->setName($name);
        $this->setTag('div');
        $this->setType('clevis.View');
        $this->addClass('clevis-view');
        $this->setIdAutoGenerated(true);
        $this->setHelpIdRequired(true);
        $this->setItemGroup('view');
        $this->setItemRoot(true);
        if ( Clevis_View_ParentInfo::isParentInfo() )
            $this->setParent(Clevis_View_ParentInfo::getParentInfo()->getParent());
        // View is always data item (it will connect to first data parent)
        $this->dataSetIsItem(true);

        // Set default permission write to true
        $this->permissionWrite = true;

        // Set last view
        self::$lastView = $this;
    }

    /**
     * Get zend view
     * 
     * @return Zend_View
     */
    public function & getZendView() {
        return $this->zendView;
    }

    /**
     * Set permission code (set tree root permission)
     * 
     * @param $permission
     * @return void
     */
    public function setPermission($permission) {
        if ( Clevis_Permission::getInstance()->hasPermissionRead($permission) == false )
            throw new Exception('Tuto stránku nemáte právo zobrazit!');
        $this->permissionWrite = Clevis_Permission::getInstance()->hasPermissionWrite($permission);
    }

    /**
     * Has permission write (must be overrided to not to cycle)
     * 
     * @return bool
     */
    public function hasPermissionWrite() {
        return $this->permissionWrite;
    }

    /**
     * Add dialog to view
     *
     * @return Clevis_View_Dialog
     */
    public function & addDialog() {
        $dialog = new Clevis_View_Dialog();
        return $this->addItem($dialog);
    }
    
    /**
     * Set page fullsize
     * 
     * @param boolean $pageFullSize 
     */
    public function setPageFullSize($pageFullSize) {
        $this->pageFullSize = $pageFullSize;
    }
    
    /**
     * Is page fullsize
     * 
     * @return boolean
     */
    public function isPageFullSize() {
        return $this->pageFullSize;
    }

    /**
     *
     */
    public function useDynamicParam($dynamicParam) {

        $dynamicParamValue = Clevis_Helper::getRequestParam($dynamicParam);
        if ( $dynamicParamValue != null && $dynamicParamValue != '' ) {
            $this->addScript(
                'require(["clevis/dynamicUrl", "clevis/ready!"], function(dynamicUrl){' . PHP_EOL .
                '    dynamicUrl.registerParam("' . $dynamicParam . '", function(){ return "' . $dynamicParamValue . '"; });' . PHP_EOL .
                '});'
            );
        }
    }

    /**
     * Render view
     * 
     * @return string
     */
    public function render() { 
        // Render
        $oldId = Clevis_View::getCurrentId();
        Clevis_View::setCurrentId($this->getId());
        $output = parent::render();
        Clevis_View::setCurrentId($oldId);

        return $output;
    }

    /**
     * Render top view
     * 
     * @param Zend_View $zendView
     */
    public function renderTopView(Zend_View & $zendView) {
        Clevis_Timer::start('run-action-render');
        
        $this->zendView = &$zendView;
        $output = '';

        // TODO: a co se stane kdyz je $item null???? stat se rozhodne muze ne vsechny stranky jsou v navigaci.
        $request = Zend_Controller_Front::getInstance()->getRequest();
        $code = '';
        if ( $request->getParam('code') != null )
            $code = '?code=' . $request->getParam('code');

        $url = '/' . $request->getParam('module') . '/' . $request->getParam('controller') . '/' . $request->getParam('action') . $code;
        $dialog = new Common_View_DialogFeedback($url);
        $this->addItem($dialog);

        // Pre render
        $output .= PHP_EOL;
        $output .= '<!-- PRE-RENDER BEGIN -->' . PHP_EOL;
        $output .= '<div id="clevis_render_pre" style="position: absolute; top: -9999px; left: -9999px; bottom: 9999px; right: 9999px;">' . PHP_EOL;
        $output .= $this->preRender();
        $output .= Clevis_Layout::renderViewPrepare($this);
        $output .= '</div>' . PHP_EOL;
        $output .= '<!-- PRE-RENDER END -->' . PHP_EOL;

        // Render
        $output .= PHP_EOL;
        $output .= '<!-- RENDER BEGIN -->' . PHP_EOL;
        $output .= $this->render();
        $output .= '<!-- RENDER END -->' . PHP_EOL;
        
        $output .= PHP_EOL;
        
        Clevis_Timer::stop('run-action-render');
        
        return $output;
    }

    /**
     * Current view id
     *
     * @var string
     */
    private static $currentId;

    /**
     * Set current view id
     * 
     * @param string $currentId 
     */
    protected static function setCurrentId($currentId) {
        Clevis_View::$currentId = $currentId;
    }

    /**
     * Get current view id
     * 
     * @return string
     */
    public static function getCurrentId() {
        return Clevis_view::$currentId;
    }

    /**
     * Last view
     *
     * @var Clevis_View
     */
    private static $lastView = null;

    /**
     * Get last view
     * 
     * @return Clevis_View|null
     */
    public static function getLastView() {
        return self::$lastView;
    }

    /**
     * Render page title menu
     *
     * @param Clevis_NavigationItem $navigationItem
     * @param Clevis_View_Container $container
     * @return Clevis_View_Container
     */
    public static function renderTitleMenu(Clevis_NavigationItem $navigationItem, Clevis_View_Container $container, $viewNavigation = false) {
        return;
        if ( $navigationItem->getTitleMenuVisible() == false || $navigationItem->getCode() == 'item-individual-protection' ) {
            return null;
        }

        $currentNavigationItem = $navigationItem;
        if ( $viewNavigation != null ) {
            $navigationItem = $navigationItem->getParent();
        }

        // Prepare children
        $children = array();
        if ( count($navigationItem->getChildren()) == 0 ) {
            $parent = $navigationItem->getParent();
            // Do not iterate root
            if ( $parent != null && $parent->getParent() != null ) {
                foreach ( $parent->getChildren() as $child ) {
                    $children[$child->getUrl()] = $child;
                }
                // Add parent but only if url is filled (we don't want menu parent here)
                if ( $parent->isUrlFilled() )
                    $children = array_merge(array($parent->getUrl() => $parent), $children);
            }
        } else {
            foreach ( $navigationItem->getChildren() as $child ) {
                $children[$child->getUrl()] = $child;
            }
            if ( !$viewNavigation )
                $children = array_merge(array($navigationItem->getUrl() => $navigationItem), $children);
        }

        // Add children links
        $output = '';
        if ( count($children) > 0 ) {
            foreach ( $children as $index => $child ) {
                $url = $child->getUrl();
                if ( $url == null )
                    $url = '?code=' . $child->getCode();
                if ( $child == $currentNavigationItem ) {
                    $output .= '<a class="clevis-view-page-title-menu-current">' . $child->getName() . '</a>';
                } else {

                    if ( $child->getTitleMenuVisible() )
                        $output .= Clevis_DynamicUrl::render($url, $child->getName());
                }
            }
        }

        // Add element to container
        $titleMenu = $container->addElementDiv();
        $titleMenu->addClass('clevis-view-page-title-menu');
        $titleMenu->addClass('clevis-hidden');
        $titleMenu->addCode($output);

        return $titleMenu;
    }
}

